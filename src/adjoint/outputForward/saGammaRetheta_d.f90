!  differentiation of resscale in forward (tangent) mode (with options i4 dr8 r8):
!   variations   of useful results: *dw
!   with respect to varying inputs: *dw *scratch
!   rw status of diff variables: *dw:in-out *scratch:in
!   plus diff mem management of: dw:in scratch:in
  subroutine resscale_d()
!
!  multiply the residual by the volume and store this in dw; this
! * is done for monitoring reasons only. the multiplication with the
! * volume is present to be consistent with the flow residuals; also
!  the negative value is taken, again to be consistent with the
! * flow equations. also multiply by iblank so that no updates occur
!  in holes or the overset boundary.
    use constants
    use blockpointers
    implicit none
! local variables
    integer(kind=inttype) :: i, j, k, ii
    real(kind=realtype) :: rblank
    intrinsic real
    intrinsic max
    real(realtype) :: x1
    real(kind=realtype) :: temp
    do k=2,kl
      do j=2,jl
        do i=2,il
          x1 = real(iblank(i, j, k), realtype)
          if (x1 .lt. zero) then
            rblank = zero
          else
            rblank = x1
          end if
! sa variable ν~
          temp = volref(i, j, k)*rblank
          dwd(i, j, k, itu1) = -(temp*scratchd(i, j, k, idvt))
          dw(i, j, k, itu1) = -(temp*scratch(i, j, k, idvt))
! gamma
          temp = volref(i, j, k)*rblank
          dwd(i, j, k, itu2) = -(temp*scratchd(i, j, k, idvt+1))
          dw(i, j, k, itu2) = -(temp*scratch(i, j, k, idvt+1))
! retheta
          temp = volref(i, j, k)*rblank
          dwd(i, j, k, itu3) = -(temp*scratchd(i, j, k, idvt+2))
          dw(i, j, k, itu3) = -(temp*scratch(i, j, k, idvt+2))
        end do
      end do
    end do
  end subroutine resscale_d

  subroutine resscale()
!
!  multiply the residual by the volume and store this in dw; this
! * is done for monitoring reasons only. the multiplication with the
! * volume is present to be consistent with the flow residuals; also
!  the negative value is taken, again to be consistent with the
! * flow equations. also multiply by iblank so that no updates occur
!  in holes or the overset boundary.
    use constants
    use blockpointers
    implicit none
! local variables
    integer(kind=inttype) :: i, j, k, ii
    real(kind=realtype) :: rblank
    intrinsic real
    intrinsic max
    real(realtype) :: x1
    do k=2,kl
      do j=2,jl
        do i=2,il
          x1 = real(iblank(i, j, k), realtype)
          if (x1 .lt. zero) then
            rblank = zero
          else
            rblank = x1
          end if
! sa variable ν~
          dw(i, j, k, itu1) = -(volref(i, j, k)*scratch(i, j, k, idvt)*&
&           rblank)
! gamma
          dw(i, j, k, itu2) = -(volref(i, j, k)*scratch(i, j, k, idvt+1)&
&           *rblank)
! retheta
          dw(i, j, k, itu3) = -(volref(i, j, k)*scratch(i, j, k, idvt+2)&
&           *rblank)
        end do
      end do
    end do
  end subroutine resscale

  subroutine sagammarethetasolve(resonly)
!
!       coupled dd-adi sa-gamma-retheta transport solve.
!       solves the 3-equation system using a diagonally-dominant
!       alternating-direction-implicit scheme with full 3x3 block
!       coupling, following the same pattern as the sst solver.
!
    use blockpointers
    use constants
    use flowvarrefstate
    use inputiteration
    use inputphysics
    use paramturb
    use turbutils_d, only : tdia3x3
    implicit none
!
!      subroutine arguments.
!
    logical, intent(in) :: resonly
!
!      local variables.
!
    integer(kind=inttype) :: i, j, k
! viscosity variables
    real(kind=realtype) :: nu, nu_m, nu_p
    real(kind=realtype) :: nut, nut_m, nut_p
    real(kind=realtype) :: nu_tm, nu_tp
    real(kind=realtype) :: nutilde, nutilde_m, nutilde_p
    real(kind=realtype) :: chi, chi3, fv1
    real(kind=realtype) :: chi_m, chi3_m, fv1_m
    real(kind=realtype) :: chi_p, chi3_p, fv1_p
! geometry terms
    real(kind=realtype) :: voli, volmi, volpi
    real(kind=realtype) :: xm, ym, zm, xp, yp, zp, xa, ya, za
    real(kind=realtype) :: ttm, ttp
! diffusion coefficients
    real(kind=realtype) :: num_v, nup_v
    real(kind=realtype) :: cdm, cdp
    real(kind=realtype) :: cdm_gamma, cdp_gamma
    real(kind=realtype) :: cdm_rt, cdp_rt
! sa nonlinear correction
    real(kind=realtype) :: cnud, cam, cap
    real(kind=realtype) :: nutm, nutp
! clipped diffusion coefficients
    real(kind=realtype) :: c1m, c1p, c2m, c2p, c3m, c3p
! advection
    real(kind=realtype) :: qs, uu, um, up
! constants
    real(kind=realtype) :: cb3inv, cv13
! misc
    real(kind=realtype) :: rblank, factor
    real(kind=realtype) :: gammanew, gammadelta, dampfactor
    integer(kind=inttype) :: mm
! eigenvalue-based dt cap variables
    real(kind=realtype) :: lambda_eig, dt_cap_inv
    real(kind=realtype) :: j2(2, 2), j3(3, 3)
! scaling values from existing turbulence residual scaling options
    real(kind=realtype) :: scalenu, scalegamma, scaleretheta
! scaling ratios (precomputed from scaling values)
    real(kind=realtype) :: s12, s13, s21, s23, s31, s32
    intrinsic max
! adi work arrays
    real(kind=realtype), dimension(3, 2:max(il, jl, kl)) :: bb, dd, ff
    real(kind=realtype), dimension(3, 3, 2:max(il, jl, kl)) :: cc
    logical, save :: printedcoupling=.false.
    intrinsic abs
    intrinsic real
    intrinsic min
    real(kind=realtype) :: x1
    real(kind=realtype) :: x2
    real(kind=realtype) :: x3
    real(kind=realtype) :: x4
    if (resonly) then
      return
    else
      if (.not.printedcoupling) then
        printedcoupling = .true.
        select case  (turbdadicoupled) 
        case (0) 
          print*, &
&      'sa-gamma-retheta dadi coupling: fully decoupled (diagonal only)'
        case (1) 
          print*, &
&  'sa-gamma-retheta dadi coupling: sa decoupled, gamma-retheta coupled'
        case (2) 
          print*, &
&         'sa-gamma-retheta dadi coupling: fully coupled (3x3 block)'
        case default
          print*, 'sa-gamma-retheta dadi coupling: unknown mode', &
&         turbdadicoupled
        end select
      end if
      cb3inv = one/rsacb3
      cv13 = rsacv1**3
      if (turbresscale(1) .ge. 0.) then
        x1 = turbresscale(1)
      else
        x1 = -turbresscale(1)
      end if
      if (x1 .lt. one) then
        scalenu = one
      else
        scalenu = x1
      end if
      if (turbresscale(2) .ge. 0.) then
        x2 = turbresscale(2)
      else
        x2 = -turbresscale(2)
      end if
      if (x2 .lt. one) then
        scalegamma = one
      else
        scalegamma = x2
      end if
      if (turbresscale(3) .ge. 0.) then
        x3 = turbresscale(3)
      else
        x3 = -turbresscale(3)
      end if
      if (x3 .lt. one) then
        scaleretheta = one
      else
        scaleretheta = x3
      end if
! precompute scaling ratios: s_col / s_row for off-diagonal entries
! eq1, var2
      s12 = scalegamma/scalenu
! eq1, var3
      s13 = scaleretheta/scalenu
! eq2, var1
      s21 = scalenu/scalegamma
! eq2, var3
      s23 = scaleretheta/scalegamma
! eq3, var1
      s31 = scalenu/scaleretheta
! eq3, var2
      s32 = scalegamma/scaleretheta
! prepare qq: decoupled mode, scaling, and cfl factor.
! for implicit relaxation: factor = 1 + (1-alfa)/alfa = 1/alfa.
      factor = one
      if (turbrelax .eq. turbrelaximplicit) factor = one + (one-alfaturb&
&         )/alfaturb
      do k=2,kl
        do j=2,jl
          do i=2,il
! turbdadicoupled: 0=decoupled, 1=transition-only, 2=full
            if (turbdadicoupled .eq. 0) then
              qq(i, j, k, 1, 2) = zero
              qq(i, j, k, 1, 3) = zero
              qq(i, j, k, 2, 1) = zero
              qq(i, j, k, 2, 3) = zero
              qq(i, j, k, 3, 1) = zero
              qq(i, j, k, 3, 2) = zero
            else if (turbdadicoupled .eq. 1) then
! sa decoupled, gamma-retheta coupled
              qq(i, j, k, 1, 2) = zero
              qq(i, j, k, 1, 3) = zero
              qq(i, j, k, 2, 1) = zero
              qq(i, j, k, 3, 1) = zero
            end if
! eigenvalue-based source dt restriction (eq. 59)
            if (transitionsrcdtrestrict .and. turbdadicoupled .gt. 0) &
&           then
              if (turbdadicoupled .eq. 1) then
! 2x2 eigenvalue of gamma-retheta sub-block
                j2(1, 1) = qq(i, j, k, 2, 2)
                j2(1, 2) = qq(i, j, k, 2, 3)
                j2(2, 1) = qq(i, j, k, 3, 2)
                j2(2, 2) = qq(i, j, k, 3, 3)
                lambda_eig = maxposeigenvalue2x2(j2)
                dt_cap_inv = lambda_eig/rsagrsrcdtlimit
                if (qq(i, j, k, 2, 2) .lt. dt_cap_inv) then
                  qq(i, j, k, 2, 2) = dt_cap_inv
                else
                  qq(i, j, k, 2, 2) = qq(i, j, k, 2, 2)
                end if
                if (qq(i, j, k, 3, 3) .lt. dt_cap_inv) then
                  qq(i, j, k, 3, 3) = dt_cap_inv
                else
                  qq(i, j, k, 3, 3) = qq(i, j, k, 3, 3)
                end if
              else
! full 3x3 eigenvalue
                j3(1, 1) = qq(i, j, k, 1, 1)
                j3(1, 2) = qq(i, j, k, 1, 2)
                j3(1, 3) = qq(i, j, k, 1, 3)
                j3(2, 1) = qq(i, j, k, 2, 1)
                j3(2, 2) = qq(i, j, k, 2, 2)
                j3(2, 3) = qq(i, j, k, 2, 3)
                j3(3, 1) = qq(i, j, k, 3, 1)
                j3(3, 2) = qq(i, j, k, 3, 2)
                j3(3, 3) = qq(i, j, k, 3, 3)
                lambda_eig = maxposeigenvalue3x3(j3)
                dt_cap_inv = lambda_eig/rsagrsrcdtlimit
                if (qq(i, j, k, 1, 1) .lt. dt_cap_inv) then
                  qq(i, j, k, 1, 1) = dt_cap_inv
                else
                  qq(i, j, k, 1, 1) = qq(i, j, k, 1, 1)
                end if
                if (qq(i, j, k, 2, 2) .lt. dt_cap_inv) then
                  qq(i, j, k, 2, 2) = dt_cap_inv
                else
                  qq(i, j, k, 2, 2) = qq(i, j, k, 2, 2)
                end if
                if (qq(i, j, k, 3, 3) .lt. dt_cap_inv) then
                  qq(i, j, k, 3, 3) = dt_cap_inv
                else
                  qq(i, j, k, 3, 3) = qq(i, j, k, 3, 3)
                end if
              end if
            end if
! symmetric scaling (§4): qq(m,n) *= s_n / s_m
! diagonal entries unchanged; only off-diag scaled.
            qq(i, j, k, 1, 2) = qq(i, j, k, 1, 2)*s12
            qq(i, j, k, 1, 3) = qq(i, j, k, 1, 3)*s13
            qq(i, j, k, 2, 1) = qq(i, j, k, 2, 1)*s21
            qq(i, j, k, 2, 3) = qq(i, j, k, 2, 3)*s23
            qq(i, j, k, 3, 1) = qq(i, j, k, 3, 1)*s31
            qq(i, j, k, 3, 2) = qq(i, j, k, 3, 2)*s32
! cfl factor scaling (all 9 entries)
            qq(i, j, k, 1, 1) = factor*qq(i, j, k, 1, 1)
            qq(i, j, k, 1, 2) = factor*qq(i, j, k, 1, 2)
            qq(i, j, k, 1, 3) = factor*qq(i, j, k, 1, 3)
            qq(i, j, k, 2, 1) = factor*qq(i, j, k, 2, 1)
            qq(i, j, k, 2, 2) = factor*qq(i, j, k, 2, 2)
            qq(i, j, k, 2, 3) = factor*qq(i, j, k, 2, 3)
            qq(i, j, k, 3, 1) = factor*qq(i, j, k, 3, 1)
            qq(i, j, k, 3, 2) = factor*qq(i, j, k, 3, 2)
            qq(i, j, k, 3, 3) = factor*qq(i, j, k, 3, 3)
! scale the rhs: scratch(m) /= s_m
            scratch(i, j, k, idvt) = scratch(i, j, k, idvt)/scalenu
            scratch(i, j, k, idvt+1) = scratch(i, j, k, idvt+1)/&
&             scalegamma
            scratch(i, j, k, idvt+2) = scratch(i, j, k, idvt+2)/&
&             scaleretheta
          end do
        end do
      end do
! initialize grid velocity to zero.
      qs = zero
!
!       dd-adi step in j-direction. as we solve in j-direction,
!       the j-loop is the innermost loop.
!
      do k=2,kl
        do i=2,il
          do j=2,jl
! recompute viscous diffusion coefficients in j-direction.
            voli = one/vol(i, j, k)
            volmi = two/(vol(i, j, k)+vol(i, j-1, k))
            volpi = two/(vol(i, j, k)+vol(i, j+1, k))
            xm = sj(i, j-1, k, 1)*volmi
            ym = sj(i, j-1, k, 2)*volmi
            zm = sj(i, j-1, k, 3)*volmi
            xp = sj(i, j, k, 1)*volpi
            yp = sj(i, j, k, 2)*volpi
            zp = sj(i, j, k, 3)*volpi
            xa = half*(sj(i, j, k, 1)+sj(i, j-1, k, 1))*voli
            ya = half*(sj(i, j, k, 2)+sj(i, j-1, k, 2))*voli
            za = half*(sj(i, j, k, 3)+sj(i, j-1, k, 3))*voli
            ttm = xm*xa + ym*ya + zm*za
            ttp = xp*xa + yp*ya + zp*za
            cnud = -(rsacb2*w(i, j, k, itu1)*cb3inv)
            cam = ttm*cnud
            cap = ttp*cnud
            nutm = half*(w(i, j-1, k, itu1)+w(i, j, k, itu1))
            nutp = half*(w(i, j+1, k, itu1)+w(i, j, k, itu1))
            nu = rlv(i, j, k)/w(i, j, k, irho)
            nutilde = w(i, j, k, itu1)
            chi = nutilde/nu
            chi3 = chi*chi*chi
            fv1 = chi3/(chi3+cv13)
            nut = nutilde*fv1
            nu_m = rlv(i, j-1, k)/w(i, j-1, k, irho)
            nutilde_m = w(i, j-1, k, itu1)
            chi_m = nutilde_m/nu_m
            chi3_m = chi_m*chi_m*chi_m
            fv1_m = chi3_m/(chi3_m+cv13)
            nut_m = nutilde_m*fv1_m
            nu_p = rlv(i, j+1, k)/w(i, j+1, k, irho)
            nutilde_p = w(i, j+1, k, itu1)
            chi_p = nutilde_p/nu_p
            chi3_p = chi_p*chi_p*chi_p
            fv1_p = chi3_p/(chi3_p+cv13)
            nut_p = nutilde_p*fv1_p
            num_v = half*(nu_m+nu)
            nup_v = half*(nu_p+nu)
            nu_tm = half*(nut_m+nut)
            nu_tp = half*(nut_p+nut)
! sa diffusion
            cdm = (num_v+(one+rsacb2)*nutm)*ttm*cb3inv
            cdp = (nup_v+(one+rsacb2)*nutp)*ttp*cb3inv
! gamma diffusion
            cdm_gamma = (num_v+nu_tm/sigmaf)*ttm
            cdp_gamma = (nup_v+nu_tp/sigmaf)*ttp
! retheta diffusion
            cdm_rt = sigmatheta*(num_v+nu_tm)*ttm
            cdp_rt = sigmatheta*(nup_v+nu_tp)*ttp
            if (cdm + cam .lt. zero) then
              c1m = zero
            else
              c1m = cdm + cam
            end if
            if (cdp + cap .lt. zero) then
              c1p = zero
            else
              c1p = cdp + cap
            end if
            if (cdm_gamma .lt. zero) then
              c2m = zero
            else
              c2m = cdm_gamma
            end if
            if (cdp_gamma .lt. zero) then
              c2p = zero
            else
              c2p = cdp_gamma
            end if
            if (cdm_rt .lt. zero) then
              c3m = zero
            else
              c3m = cdm_rt
            end if
            if (cdp_rt .lt. zero) then
              c3p = zero
            else
              c3p = cdp_rt
            end if
            bb(1, j) = -c1m
            dd(1, j) = -c1p
            bb(2, j) = -c2m
            dd(2, j) = -c2p
            bb(3, j) = -c3m
            dd(3, j) = -c3p
! add advection off-diagonal terms in j-direction.
            if (addgridvelocities) qs = half*(sfacej(i, j, k)+sfacej(i, &
&               j-1, k))*voli
            uu = xa*w(i, j, k, ivx) + ya*w(i, j, k, ivy) + za*w(i, j, k&
&             , ivz) - qs
            um = zero
            up = zero
            if (uu .lt. zero) um = uu
            if (uu .gt. zero) up = uu
            bb(1, j) = bb(1, j) - up
            dd(1, j) = dd(1, j) + um
            bb(2, j) = bb(2, j) - up
            dd(2, j) = dd(2, j) + um
            bb(3, j) = bb(3, j) - up
            dd(3, j) = dd(3, j) + um
! store central jacobian and rhs in cc and ff.
! multiply off-diagonal qq entries and rhs by iblank
! so the update for iblank=0 cells is zero.
            rblank = real(iblank(i, j, k), realtype)
            cc(1, 1, j) = qq(i, j, k, 1, 1)
            cc(1, 2, j) = qq(i, j, k, 1, 2)*rblank
            cc(1, 3, j) = qq(i, j, k, 1, 3)*rblank
            cc(2, 1, j) = qq(i, j, k, 2, 1)*rblank
            cc(2, 2, j) = qq(i, j, k, 2, 2)
            cc(2, 3, j) = qq(i, j, k, 2, 3)*rblank
            cc(3, 1, j) = qq(i, j, k, 3, 1)*rblank
            cc(3, 2, j) = qq(i, j, k, 3, 2)*rblank
            cc(3, 3, j) = qq(i, j, k, 3, 3)
            ff(1, j) = scratch(i, j, k, idvt)*rblank
            ff(2, j) = scratch(i, j, k, idvt+1)*rblank
            ff(3, j) = scratch(i, j, k, idvt+2)*rblank
            bb(:, j) = bb(:, j)*rblank
            dd(:, j) = dd(:, j)*rblank
          end do
! solve the tri-diagonal system in j-direction.
          call tdia3x3(2_inttype, jl, bb, cc, dd, ff)
! determine the new rhs for the next direction.
          do j=2,jl
            scratch(i, j, k, idvt) = qq(i, j, k, 1, 1)*ff(1, j) + qq(i, &
&             j, k, 1, 2)*ff(2, j) + qq(i, j, k, 1, 3)*ff(3, j)
            scratch(i, j, k, idvt+1) = qq(i, j, k, 2, 1)*ff(1, j) + qq(i&
&             , j, k, 2, 2)*ff(2, j) + qq(i, j, k, 2, 3)*ff(3, j)
            scratch(i, j, k, idvt+2) = qq(i, j, k, 3, 1)*ff(1, j) + qq(i&
&             , j, k, 3, 2)*ff(2, j) + qq(i, j, k, 3, 3)*ff(3, j)
          end do
        end do
      end do
!
!       dd-adi step in i-direction. as we solve in i-direction,
!       the i-loop is the innermost loop.
!
      do k=2,kl
        do j=2,jl
          do i=2,il
! recompute viscous diffusion coefficients in i-direction.
            voli = one/vol(i, j, k)
            volmi = two/(vol(i, j, k)+vol(i-1, j, k))
            volpi = two/(vol(i, j, k)+vol(i+1, j, k))
            xm = si(i-1, j, k, 1)*volmi
            ym = si(i-1, j, k, 2)*volmi
            zm = si(i-1, j, k, 3)*volmi
            xp = si(i, j, k, 1)*volpi
            yp = si(i, j, k, 2)*volpi
            zp = si(i, j, k, 3)*volpi
            xa = half*(si(i, j, k, 1)+si(i-1, j, k, 1))*voli
            ya = half*(si(i, j, k, 2)+si(i-1, j, k, 2))*voli
            za = half*(si(i, j, k, 3)+si(i-1, j, k, 3))*voli
            ttm = xm*xa + ym*ya + zm*za
            ttp = xp*xa + yp*ya + zp*za
            cnud = -(rsacb2*w(i, j, k, itu1)*cb3inv)
            cam = ttm*cnud
            cap = ttp*cnud
            nutm = half*(w(i-1, j, k, itu1)+w(i, j, k, itu1))
            nutp = half*(w(i+1, j, k, itu1)+w(i, j, k, itu1))
            nu = rlv(i, j, k)/w(i, j, k, irho)
            nutilde = w(i, j, k, itu1)
            chi = nutilde/nu
            chi3 = chi*chi*chi
            fv1 = chi3/(chi3+cv13)
            nut = nutilde*fv1
            nu_m = rlv(i-1, j, k)/w(i-1, j, k, irho)
            nutilde_m = w(i-1, j, k, itu1)
            chi_m = nutilde_m/nu_m
            chi3_m = chi_m*chi_m*chi_m
            fv1_m = chi3_m/(chi3_m+cv13)
            nut_m = nutilde_m*fv1_m
            nu_p = rlv(i+1, j, k)/w(i+1, j, k, irho)
            nutilde_p = w(i+1, j, k, itu1)
            chi_p = nutilde_p/nu_p
            chi3_p = chi_p*chi_p*chi_p
            fv1_p = chi3_p/(chi3_p+cv13)
            nut_p = nutilde_p*fv1_p
            num_v = half*(nu_m+nu)
            nup_v = half*(nu_p+nu)
            nu_tm = half*(nut_m+nut)
            nu_tp = half*(nut_p+nut)
! sa diffusion
            cdm = (num_v+(one+rsacb2)*nutm)*ttm*cb3inv
            cdp = (nup_v+(one+rsacb2)*nutp)*ttp*cb3inv
! gamma diffusion
            cdm_gamma = (num_v+nu_tm/sigmaf)*ttm
            cdp_gamma = (nup_v+nu_tp/sigmaf)*ttp
! retheta diffusion
            cdm_rt = sigmatheta*(num_v+nu_tm)*ttm
            cdp_rt = sigmatheta*(nup_v+nu_tp)*ttp
            if (cdm + cam .lt. zero) then
              c1m = zero
            else
              c1m = cdm + cam
            end if
            if (cdp + cap .lt. zero) then
              c1p = zero
            else
              c1p = cdp + cap
            end if
            if (cdm_gamma .lt. zero) then
              c2m = zero
            else
              c2m = cdm_gamma
            end if
            if (cdp_gamma .lt. zero) then
              c2p = zero
            else
              c2p = cdp_gamma
            end if
            if (cdm_rt .lt. zero) then
              c3m = zero
            else
              c3m = cdm_rt
            end if
            if (cdp_rt .lt. zero) then
              c3p = zero
            else
              c3p = cdp_rt
            end if
            bb(1, i) = -c1m
            dd(1, i) = -c1p
            bb(2, i) = -c2m
            dd(2, i) = -c2p
            bb(3, i) = -c3m
            dd(3, i) = -c3p
! add advection off-diagonal terms in i-direction.
            if (addgridvelocities) qs = half*(sfacei(i, j, k)+sfacei(i-1&
&               , j, k))*voli
            uu = xa*w(i, j, k, ivx) + ya*w(i, j, k, ivy) + za*w(i, j, k&
&             , ivz) - qs
            um = zero
            up = zero
            if (uu .lt. zero) um = uu
            if (uu .gt. zero) up = uu
            bb(1, i) = bb(1, i) - up
            dd(1, i) = dd(1, i) + um
            bb(2, i) = bb(2, i) - up
            dd(2, i) = dd(2, i) + um
            bb(3, i) = bb(3, i) - up
            dd(3, i) = dd(3, i) + um
! store central jacobian and rhs in cc and ff.
            rblank = real(iblank(i, j, k), realtype)
            cc(1, 1, i) = qq(i, j, k, 1, 1)
            cc(1, 2, i) = qq(i, j, k, 1, 2)*rblank
            cc(1, 3, i) = qq(i, j, k, 1, 3)*rblank
            cc(2, 1, i) = qq(i, j, k, 2, 1)*rblank
            cc(2, 2, i) = qq(i, j, k, 2, 2)
            cc(2, 3, i) = qq(i, j, k, 2, 3)*rblank
            cc(3, 1, i) = qq(i, j, k, 3, 1)*rblank
            cc(3, 2, i) = qq(i, j, k, 3, 2)*rblank
            cc(3, 3, i) = qq(i, j, k, 3, 3)
            ff(1, i) = scratch(i, j, k, idvt)*rblank
            ff(2, i) = scratch(i, j, k, idvt+1)*rblank
            ff(3, i) = scratch(i, j, k, idvt+2)*rblank
            bb(:, i) = bb(:, i)*rblank
            dd(:, i) = dd(:, i)*rblank
          end do
! solve the tri-diagonal system in i-direction.
          call tdia3x3(2_inttype, il, bb, cc, dd, ff)
! determine the new rhs for the next direction.
          do i=2,il
            scratch(i, j, k, idvt) = qq(i, j, k, 1, 1)*ff(1, i) + qq(i, &
&             j, k, 1, 2)*ff(2, i) + qq(i, j, k, 1, 3)*ff(3, i)
            scratch(i, j, k, idvt+1) = qq(i, j, k, 2, 1)*ff(1, i) + qq(i&
&             , j, k, 2, 2)*ff(2, i) + qq(i, j, k, 2, 3)*ff(3, i)
            scratch(i, j, k, idvt+2) = qq(i, j, k, 3, 1)*ff(1, i) + qq(i&
&             , j, k, 3, 2)*ff(2, i) + qq(i, j, k, 3, 3)*ff(3, i)
          end do
        end do
      end do
!
!       dd-adi step in k-direction. as we solve in k-direction,
!       the k-loop is the innermost loop.
!
      do j=2,jl
        do i=2,il
          do k=2,kl
! recompute viscous diffusion coefficients in k-direction.
            voli = one/vol(i, j, k)
            volmi = two/(vol(i, j, k)+vol(i, j, k-1))
            volpi = two/(vol(i, j, k)+vol(i, j, k+1))
            xm = sk(i, j, k-1, 1)*volmi
            ym = sk(i, j, k-1, 2)*volmi
            zm = sk(i, j, k-1, 3)*volmi
            xp = sk(i, j, k, 1)*volpi
            yp = sk(i, j, k, 2)*volpi
            zp = sk(i, j, k, 3)*volpi
            xa = half*(sk(i, j, k, 1)+sk(i, j, k-1, 1))*voli
            ya = half*(sk(i, j, k, 2)+sk(i, j, k-1, 2))*voli
            za = half*(sk(i, j, k, 3)+sk(i, j, k-1, 3))*voli
            ttm = xm*xa + ym*ya + zm*za
            ttp = xp*xa + yp*ya + zp*za
            cnud = -(rsacb2*w(i, j, k, itu1)*cb3inv)
            cam = ttm*cnud
            cap = ttp*cnud
            nutm = half*(w(i, j, k-1, itu1)+w(i, j, k, itu1))
            nutp = half*(w(i, j, k+1, itu1)+w(i, j, k, itu1))
            nu = rlv(i, j, k)/w(i, j, k, irho)
            nutilde = w(i, j, k, itu1)
            chi = nutilde/nu
            chi3 = chi*chi*chi
            fv1 = chi3/(chi3+cv13)
            nut = nutilde*fv1
            nu_m = rlv(i, j, k-1)/w(i, j, k-1, irho)
            nutilde_m = w(i, j, k-1, itu1)
            chi_m = nutilde_m/nu_m
            chi3_m = chi_m*chi_m*chi_m
            fv1_m = chi3_m/(chi3_m+cv13)
            nut_m = nutilde_m*fv1_m
            nu_p = rlv(i, j, k+1)/w(i, j, k+1, irho)
            nutilde_p = w(i, j, k+1, itu1)
            chi_p = nutilde_p/nu_p
            chi3_p = chi_p*chi_p*chi_p
            fv1_p = chi3_p/(chi3_p+cv13)
            nut_p = nutilde_p*fv1_p
            num_v = half*(nu_m+nu)
            nup_v = half*(nu_p+nu)
            nu_tm = half*(nut_m+nut)
            nu_tp = half*(nut_p+nut)
! sa diffusion
            cdm = (num_v+(one+rsacb2)*nutm)*ttm*cb3inv
            cdp = (nup_v+(one+rsacb2)*nutp)*ttp*cb3inv
! gamma diffusion
            cdm_gamma = (num_v+nu_tm/sigmaf)*ttm
            cdp_gamma = (nup_v+nu_tp/sigmaf)*ttp
! retheta diffusion
            cdm_rt = sigmatheta*(num_v+nu_tm)*ttm
            cdp_rt = sigmatheta*(nup_v+nu_tp)*ttp
            if (cdm + cam .lt. zero) then
              c1m = zero
            else
              c1m = cdm + cam
            end if
            if (cdp + cap .lt. zero) then
              c1p = zero
            else
              c1p = cdp + cap
            end if
            if (cdm_gamma .lt. zero) then
              c2m = zero
            else
              c2m = cdm_gamma
            end if
            if (cdp_gamma .lt. zero) then
              c2p = zero
            else
              c2p = cdp_gamma
            end if
            if (cdm_rt .lt. zero) then
              c3m = zero
            else
              c3m = cdm_rt
            end if
            if (cdp_rt .lt. zero) then
              c3p = zero
            else
              c3p = cdp_rt
            end if
            bb(1, k) = -c1m
            dd(1, k) = -c1p
            bb(2, k) = -c2m
            dd(2, k) = -c2p
            bb(3, k) = -c3m
            dd(3, k) = -c3p
! add advection off-diagonal terms in k-direction.
            if (addgridvelocities) qs = half*(sfacek(i, j, k)+sfacek(i, &
&               j, k-1))*voli
            uu = xa*w(i, j, k, ivx) + ya*w(i, j, k, ivy) + za*w(i, j, k&
&             , ivz) - qs
            um = zero
            up = zero
            if (uu .lt. zero) um = uu
            if (uu .gt. zero) up = uu
            bb(1, k) = bb(1, k) - up
            dd(1, k) = dd(1, k) + um
            bb(2, k) = bb(2, k) - up
            dd(2, k) = dd(2, k) + um
            bb(3, k) = bb(3, k) - up
            dd(3, k) = dd(3, k) + um
! store central jacobian and rhs in cc and ff.
            rblank = real(iblank(i, j, k), realtype)
            cc(1, 1, k) = qq(i, j, k, 1, 1)
            cc(1, 2, k) = qq(i, j, k, 1, 2)*rblank
            cc(1, 3, k) = qq(i, j, k, 1, 3)*rblank
            cc(2, 1, k) = qq(i, j, k, 2, 1)*rblank
            cc(2, 2, k) = qq(i, j, k, 2, 2)
            cc(2, 3, k) = qq(i, j, k, 2, 3)*rblank
            cc(3, 1, k) = qq(i, j, k, 3, 1)*rblank
            cc(3, 2, k) = qq(i, j, k, 3, 2)*rblank
            cc(3, 3, k) = qq(i, j, k, 3, 3)
            ff(1, k) = scratch(i, j, k, idvt)*rblank
            ff(2, k) = scratch(i, j, k, idvt+1)*rblank
            ff(3, k) = scratch(i, j, k, idvt+2)*rblank
            bb(:, k) = bb(:, k)*rblank
            dd(:, k) = dd(:, k)*rblank
          end do
! solve the tri-diagonal system in k-direction.
          call tdia3x3(2_inttype, kl, bb, cc, dd, ff)
! store the final update in scratch.
          do k=2,kl
            scratch(i, j, k, idvt) = ff(1, k)
            scratch(i, j, k, idvt+1) = ff(2, k)
            scratch(i, j, k, idvt+2) = ff(3, k)
          end do
        end do
      end do
!
!       update the turbulent variables. for explicit relaxation the
!       update must be relaxed; for implicit relaxation this has been
!       done via the time step.
!       unscale the update: δq(m) = s_m * δq_scaled(m).
!
      factor = one
      if (turbrelax .eq. turbrelaxexplicit) factor = alfaturb
      do k=2,kl
        do j=2,jl
          do i=2,il
            w(i, j, k, itu1) = w(i, j, k, itu1) + factor*scalenu*scratch&
&             (i, j, k, idvt)
            if (w(i, j, k, itu1) .lt. zero) then
              w(i, j, k, itu1) = zero
            else
              w(i, j, k, itu1) = w(i, j, k, itu1)
            end if
! gamma update with exponential back-off damping (§3).
! if the raw update overshoots [gammalo, gammahi],
! reduce the step by theta^m until it stays in range.
            gammadelta = factor*scalegamma*scratch(i, j, k, idvt+1)
            gammanew = w(i, j, k, itu2) + gammadelta
            dampfactor = one
            do mm=1,40
              if (gammanew .ge. rsagrgammalo .and. gammanew .le. &
&                 rsagrgammahi) then
                goto 100
              else
                dampfactor = dampfactor*rsagrdamptheta
                gammanew = w(i, j, k, itu2) + dampfactor*gammadelta
              end if
            end do
 100        if (gammanew .lt. rsagrgammalo) then
              x4 = rsagrgammalo
            else
              x4 = gammanew
            end if
            if (x4 .gt. rsagrgammahi) then
              w(i, j, k, itu2) = rsagrgammahi
            else
              w(i, j, k, itu2) = x4
            end if
! retheta update with exponential back-off damping (§3).
            gammadelta = factor*scaleretheta*scratch(i, j, k, idvt+2)
            gammanew = w(i, j, k, itu3) + gammadelta
            dampfactor = one
            do mm=1,40
              if (gammanew .ge. rsagrrethetalo) then
                goto 110
              else
                dampfactor = dampfactor*rsagrdamptheta
                gammanew = w(i, j, k, itu3) + dampfactor*gammadelta
              end if
            end do
 110        if (gammanew .lt. rsagrrethetalo) then
              w(i, j, k, itu3) = rsagrrethetalo
            else
              w(i, j, k, itu3) = gammanew
            end if
          end do
        end do
      end do
    end if
  end subroutine sagammarethetasolve

  function maxposeigenvalue2x2(a) result (lambda_max)
    use constants, only : realtype, zero
    implicit none
    real(kind=realtype), intent(in) :: a(2, 2)
    real(kind=realtype) :: lambda_max
    real(kind=realtype) :: tr, det, disc, sq, l1, l2
    intrinsic max
    intrinsic sqrt
    real(kind=realtype) :: max1
    tr = a(1, 1) + a(2, 2)
    det = a(1, 1)*a(2, 2) - a(1, 2)*a(2, 1)
    disc = tr*tr - 4.0_realtype*det
    if (disc .lt. zero) then
      max1 = zero
    else
      max1 = disc
    end if
    sq = sqrt(max1)
    l1 = (tr+sq)*0.5_realtype
    l2 = (tr-sq)*0.5_realtype
    if (l1 .lt. l2) then
      if (l2 .lt. zero) then
        lambda_max = zero
      else
        lambda_max = l2
      end if
    else if (l1 .lt. zero) then
      lambda_max = zero
    else
      lambda_max = l1
    end if
  end function maxposeigenvalue2x2

  function maxposeigenvalue3x3(a) result (lambda_max)
    use constants, only : realtype, zero, one, two, three
    implicit none
    real(kind=realtype), intent(in) :: a(3, 3)
    real(kind=realtype) :: lambda_max
    real(kind=realtype) :: a2, a1, a0, q, r, theta, sq
    real(kind=realtype) :: l1, l2, l3
    real(kind=realtype), parameter :: pi=3.141592653589793_realtype
    real(kind=realtype), parameter :: third=one/three
    intrinsic max
    intrinsic sqrt
    intrinsic min
    intrinsic acos
    intrinsic cos
    real(kind=realtype) :: y1
    real(kind=realtype) :: y2
    real(kind=realtype) :: max1
    real(kind=realtype) :: max2
    real(kind=realtype) :: max3
    real(kind=realtype) :: arg1
! characteristic polynomial: lambda^3 + a2*lambda^2 + a1*lambda + a0 = 0
    a2 = -(a(1, 1)+a(2, 2)+a(3, 3))
    a1 = a(1, 1)*a(2, 2) + a(1, 1)*a(3, 3) + a(2, 2)*a(3, 3) - a(1, 2)*a&
&     (2, 1) - a(1, 3)*a(3, 1) - a(2, 3)*a(3, 2)
    a0 = -(a(1, 1)*(a(2, 2)*a(3, 3)-a(2, 3)*a(3, 2))-a(1, 2)*(a(2, 1)*a(&
&     3, 3)-a(2, 3)*a(3, 1))+a(1, 3)*(a(2, 1)*a(3, 2)-a(2, 2)*a(3, 1)))
! cardano via trigonometric method (all roots real for real symmetric-ish matrices)
    q = (three*a1-a2*a2)/9.0_realtype
    r = (9.0_realtype*a2*a1-27.0_realtype*a0-two*a2*a2*a2)/54.0_realtype
! disc = q^3 + r^2; if disc <= 0, three real roots
    if (q*q*q + r*r .le. zero) then
      if (-q .lt. zero) then
        max1 = zero
      else
        max1 = -q
      end if
      sq = sqrt(max1)
      if (sq*sq*sq .lt. 1.0e-30_realtype) then
        max3 = 1.0e-30_realtype
      else
        max3 = sq*sq*sq
      end if
      y2 = r/max3
      if (one .gt. y2) then
        y1 = y2
      else
        y1 = one
      end if
      if (-one .lt. y1) then
        max2 = y1
      else
        max2 = -one
      end if
      theta = acos(max2)
      l1 = two*sq*cos(theta*third) - a2*third
      arg1 = (theta+two*pi)*third
      l2 = two*sq*cos(arg1) - a2*third
      arg1 = (theta+4.0_realtype*pi)*third
      l3 = two*sq*cos(arg1) - a2*third
    else
! fallback: use diagonal entries as approximation
      l1 = a(1, 1)
      l2 = a(2, 2)
      l3 = a(3, 3)
    end if
    lambda_max = max(l1, l2, l3, zero)
  end function maxposeigenvalue3x3

