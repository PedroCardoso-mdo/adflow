          temp0 = rlv(i-1, j, k)/temp1
          nu_md = (rlvd(i-1, j, k)-temp0*wd(i-1, j, k, irho))/temp1
          nu_m = temp0
          nutilde_md = wd(i-1, j, k, itu1)
          nutilde_m = w(i-1, j, k, itu1)
