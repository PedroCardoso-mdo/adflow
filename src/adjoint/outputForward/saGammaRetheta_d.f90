            temp1 = w(i, j-1, k, ivy)
            temp0 = sk(i, j, k-1, 3)
            temp = w(i, j, k-1, ivy)
            vvzd = temp10*wd(i+1, j, k, ivy) + temp9*sid(i, j, k, 3) - &
&             temp8*wd(i-1, j, k, ivy) - temp7*sid(i-1, j, k, 3) + temp6&
&             *wd(i, j+1, k, ivy) + temp5*sjd(i, j, k, 3) + temp4*wd(i, &
&             j, k+1, ivy) + temp3*skd(i, j, k, 3) - temp2*wd(i, j-1, k&
&             , ivy) - temp1*sjd(i, j-1, k, 3) - temp0*wd(i, j, k-1, ivy&
&             ) - temp*skd(i, j, k-1, 3)
            vvz = temp9*temp10 - temp7*temp8 + temp5*temp6 + temp3*temp4&
&             - temp1*temp2 - temp*temp0
! and for the gradient of w.
            temp10 = si(i, j, k, 1)
            temp9 = w(i+1, j, k, ivz)
            temp8 = si(i-1, j, k, 1)
            temp7 = w(i-1, j, k, ivz)
            temp6 = sj(i, j, k, 1)
            temp5 = w(i, j+1, k, ivz)
            temp4 = sk(i, j, k, 1)
            temp3 = w(i, j, k+1, ivz)
            temp2 = sj(i, j-1, k, 1)
            temp1 = w(i, j-1, k, ivz)
            temp0 = sk(i, j, k-1, 1)
            temp = w(i, j, k-1, ivz)
            wwxd = temp10*wd(i+1, j, k, ivz) + temp9*sid(i, j, k, 1) - &
&             temp8*wd(i-1, j, k, ivz) - temp7*sid(i-1, j, k, 1) + temp6&
&             *wd(i, j+1, k, ivz) + temp5*sjd(i, j, k, 1) + temp4*wd(i, &
&             j, k+1, ivz) + temp3*skd(i, j, k, 1) - temp2*wd(i, j-1, k&
&             , ivz) - temp1*sjd(i, j-1, k, 1) - temp0*wd(i, j, k-1, ivz&
&             ) - temp*skd(i, j, k-1, 1)
            wwx = temp9*temp10 - temp7*temp8 + temp5*temp6 + temp3*temp4&
&             - temp1*temp2 - temp*temp0
            temp10 = si(i, j, k, 2)
            temp9 = w(i+1, j, k, ivz)
            temp8 = si(i-1, j, k, 2)
            temp7 = w(i-1, j, k, ivz)
            temp6 = sj(i, j, k, 2)
            temp5 = w(i, j+1, k, ivz)
            temp4 = sk(i, j, k, 2)
            temp3 = w(i, j, k+1, ivz)
            temp2 = sj(i, j-1, k, 2)
            temp1 = w(i, j-1, k, ivz)
            temp0 = sk(i, j, k-1, 2)
            temp = w(i, j, k-1, ivz)
            wwyd = temp10*wd(i+1, j, k, ivz) + temp9*sid(i, j, k, 2) - &
&             temp8*wd(i-1, j, k, ivz) - temp7*sid(i-1, j, k, 2) + temp6&
&             *wd(i, j+1, k, ivz) + temp5*sjd(i, j, k, 2) + temp4*wd(i, &
&             j, k+1, ivz) + temp3*skd(i, j, k, 2) - temp2*wd(i, j-1, k&
&             , ivz) - temp1*sjd(i, j-1, k, 2) - temp0*wd(i, j, k-1, ivz&
&             ) - temp*skd(i, j, k-1, 2)
            wwy = temp9*temp10 - temp7*temp8 + temp5*temp6 + temp3*temp4&
&             - temp1*temp2 - temp*temp0
            temp10 = si(i, j, k, 3)
            temp9 = w(i+1, j, k, ivz)
            temp8 = si(i-1, j, k, 3)
            temp7 = w(i-1, j, k, ivz)
            temp6 = sj(i, j, k, 3)
            temp5 = w(i, j+1, k, ivz)
            temp4 = sk(i, j, k, 3)
            temp3 = w(i, j, k+1, ivz)
            temp2 = sj(i, j-1, k, 3)
            temp1 = w(i, j-1, k, ivz)
            temp0 = sk(i, j, k-1, 3)
            temp = w(i, j, k-1, ivz)
            wwzd = temp10*wd(i+1, j, k, ivz) + temp9*sid(i, j, k, 3) - &
&             temp8*wd(i-1, j, k, ivz) - temp7*sid(i-1, j, k, 3) + temp6&
&             *wd(i, j+1, k, ivz) + temp5*sjd(i, j, k, 3) + temp4*wd(i, &
&             j, k+1, ivz) + temp3*skd(i, j, k, 3) - temp2*wd(i, j-1, k&
&             , ivz) - temp1*sjd(i, j-1, k, 3) - temp0*wd(i, j, k-1, ivz&
&             ) - temp*skd(i, j, k-1, 3)
            wwz = temp9*temp10 - temp7*temp8 + temp5*temp6 + temp3*temp4&
&             - temp1*temp2 - temp*temp0
! compute the components of the stress tensor.
! the combination of the current scaling of the velocity
! gradients (2*vol) and the definition of the stress tensor,
! leads to the factor 1/(4*vol).
            temp10 = fourth/vol(i, j, k)
            factd = -(temp10*vold(i, j, k)/vol(i, j, k))
            fact = temp10
            if (turbprod .eq. strain) then
              sxxd = two*(uux*factd+fact*uuxd)
              sxx = two*fact*uux
              syyd = two*(vvy*factd+fact*vvyd)
              syy = two*fact*vvy
              szzd = two*(wwz*factd+fact*wwzd)
              szz = two*fact*wwz
              sxyd = (uuy+vvx)*factd + fact*(uuyd+vvxd)
              sxy = fact*(uuy+vvx)
              sxzd = (uuz+wwx)*factd + fact*(uuzd+wwxd)
              sxz = fact*(uuz+wwx)
              syzd = (vvz+wwy)*factd + fact*(vvzd+wwyd)
              syz = fact*(vvz+wwy)
! compute 2/3 * divergence of velocity squared
              div2d = f23*2*(sxx+syy+szz)*(sxxd+syyd+szzd)
              div2 = f23*(sxx+syy+szz)**2
! compute strain production term
              strainmag2d = two*(2*sxy*sxyd+2*sxz*sxzd+2*syz*syzd) + 2*&
&               sxx*sxxd + 2*syy*syyd + 2*szz*szzd
              strainmag2 = two*(sxy**2+sxz**2+syz**2) + sxx**2 + syy**2 &
&               + szz**2
              strainprodd = two*strainmag2d - div2d
              strainprod = two*strainmag2 - div2
              temp10 = sqrt(strainprod)
              if (strainprod .eq. 0.0_8) then
                ssd = 0.0_8
              else
                ssd = strainprodd/(2.0*temp10)
              end if
              ss = temp10
            else if (turbprod .eq. vorticity) then
! compute the three components of the vorticity vector.
! substract the part coming from the rotating frame.
              vortxd = two*((wwy-vvz)*factd+fact*(wwyd-vvzd)) - two*&
&               omegaxd
              vortx = two*fact*(wwy-vvz) - two*omegax
              vortyd = two*((uuz-wwx)*factd+fact*(uuzd-wwxd)) - two*&
&               omegayd
              vorty = two*fact*(uuz-wwx) - two*omegay
              vortzd = two*((vvx-uuy)*factd+fact*(vvxd-uuyd)) - two*&
&               omegazd
              vortz = two*fact*(vvx-uuy) - two*omegaz
! compute the vorticity production term
              vortprodd = 2*vortx*vortxd + 2*vorty*vortyd + 2*vortz*&
&               vortzd
              vortprod = vortx**2 + vorty**2 + vortz**2
! first take the square root of the production term to
! obtain the correct production term for spalart-allmaras.
! we do this to avoid if statements.
              temp10 = sqrt(vortprod)
              if (vortprod .eq. 0.0_8) then
                ssd = 0.0_8
              else
                ssd = vortprodd/(2.0*temp10)
              end if
              ss = temp10
            end if
! compute the laminar kinematic viscosity, the inverse of
! wall distance squared, the ratio chi (ratio of nutilde
! and nu) and the functions fv1 and fv2. the latter corrects
! the production term near a viscous wall.
            temp10 = w(i, j, k, irho)
            temp9 = rlv(i, j, k)/temp10
            nud = (rlvd(i, j, k)-temp9*wd(i, j, k, irho))/temp10
            nu = temp9
            temp10 = one/(d2wall(i, j, k)*d2wall(i, j, k))
            dist2invd = -(temp10*2*d2walld(i, j, k)/d2wall(i, j, k))
            dist2inv = temp10
            temp10 = w(i, j, k, itu1)/nu
            chid = (wd(i, j, k, itu1)-temp10*nud)/nu
            chi = temp10
            chi2d = 2*chi*chid
            chi2 = chi*chi
            chi3d = chi2*chid + chi*chi2d
            chi3 = chi*chi2
            temp10 = chi3/(cv13+chi3)
            fv1d = (1.0-temp10)*chi3d/(cv13+chi3)
            fv1 = temp10
            temp10 = chi/(one+chi*fv1)
            fv2d = -((chid-temp10*(fv1*chid+chi*fv1d))/(one+chi*fv1))
            fv2 = one - temp10
! the function ft2, which is designed to keep a laminar
! solution laminar. when running in fully turbulent mode
! this function should be set to 0.0.
            if (useft2sa) then
              ft2d = -(rsact3*exp(-(rsact4*chi2))*rsact4*chi2d)
              ft2 = rsact3*exp(-(rsact4*chi2))
            else
              ft2 = zero
              ft2d = 0.0_8
            end if
! correct the production term to account for the influence
! of the wall.
            temp10 = w(i, j, k, itu1)
            sstd = ssd + kar2inv*(fv2*dist2inv*wd(i, j, k, itu1)+temp10*&
&             (dist2inv*fv2d+fv2*dist2invd))
            sst = ss + kar2inv*(temp10*(fv2*dist2inv))
! add rotation term (userotationsa defined in inputparams.f90)
            if (userotationsa) then
              temp10 = sqrt(two*strainmag2)
              if (two*strainmag2 .eq. 0.0_8) then
                y1d = 0.0_8
              else
                y1d = two*strainmag2d/(2.0*temp10)
              end if
              y1 = temp10
              if (zero .gt. y1) then
                min1d = y1d
                min1 = y1
              else
                min1 = zero
                min1d = 0.0_8
              end if
              sstd = sstd + rsacrot*min1d
              sst = sst + rsacrot*min1
            end if
            if (sst .lt. xminn) then
              sst = xminn
              sstd = 0.0_8
            else
              sst = sst
            end if
! compute the function fw. the argument rr is cut off at 10
! to avoid numerical problems. this is ok, because the
! asymptotical value of fw is then already reached.
            temp10 = dist2inv/sst
            temp9 = w(i, j, k, itu1)
            rrd = kar2inv*(temp10*wd(i, j, k, itu1)+temp9*(dist2invd-&
&             temp10*sstd)/sst)
            rr = kar2inv*(temp9*temp10)
            if (rr .gt. 10.0_realtype) then
              rr = 10.0_realtype
              rrd = 0.0_8
            else
              rr = rr
            end if
            ggd = (rsacw2*(6*rr**5-1.0)+1.0)*rrd
            gg = rr + rsacw2*(rr**6-rr)
            gg6d = 6*gg**5*ggd
            gg6 = gg**6
            temp10 = (one+cw36)/(cw36+gg6)
            if (temp10 .le. 0.0_8 .and. (sixth .eq. 0.0_8 .or. sixth &
&               .ne. int(sixth))) then
              termfwd = 0.0_8
            else
              termfwd = -(sixth*temp10**(sixth-1)*temp10*gg6d/(cw36+gg6)&
&               )
            end if
            termfw = temp10**sixth
            fwsad = termfw*ggd + gg*termfwd
            fwsa = gg*termfw
            if (w(i, j, k, itu2) .lt. zero) then
              x1 = zero
              x1d = 0.0_8
            else
              x1d = wd(i, j, k, itu2)
              x1 = w(i, j, k, itu2)
            end if
            if (x1 .gt. one) then
              gammaforsa = one
              gammaforsad = 0.0_8
            else
              gammaforsad = x1d
              gammaforsa = x1
            end if
            if (approxsa) then
              term1 = zero
              term1d = 0.0_8
            else
              term1d = rsacb1*((one-ft2)*(ss*gammaforsad+gammaforsa*ssd)&
&               -gammaforsa*ss*ft2d)
              term1 = gammaforsa*rsacb1*(one-ft2)*ss
            end if
! split term2 into production and destruction parts.
! production: near-wall correction from cb1*fv2/(kappa^2*d^2)
! destruction: -cw1*fw/d^2
! gamma multiplies only production.
            temp10 = (one-ft2)*fv2 + ft2
            term2_prodd = kar2inv*rsacb1*(temp10*dist2invd+dist2inv*((&
&             one-ft2)*fv2d-(fv2-1.0)*ft2d))
            term2_prod = kar2inv*rsacb1*(dist2inv*temp10)
            term2_destd = -(rsacw1*(fwsa*dist2invd+dist2inv*fwsad))
            term2_dest = -(dist2inv*rsacw1*fwsa)
! effective term2 with gamma on production only
            term2d = term2_prod*gammaforsad + gammaforsa*term2_prodd + &
&             term2_destd
            term2 = gammaforsa*term2_prod + term2_dest
            temp10 = w(i, j, k, itu1)
            temp9 = w(i, j, k, itu1)
            scratchd(i, j, k, idvt) = temp10*(term1d+temp9*term2d+term2*&
&             wd(i, j, k, itu1)) + (term1+term2*temp9)*wd(i, j, k, itu1)
            scratch(i, j, k, idvt) = (term1+term2*temp9)*temp10
! ========================================================
! gamma and retheta source terms (slangtry-menter)
! ========================================================
! --- compute vorticity and strain magnitudes ---
            vortxd = two*((wwy-vvz)*factd+fact*(wwyd-vvzd)) - two*&
&             omegaxd
            vortx = two*fact*(wwy-vvz) - two*omegax
            vortyd = two*((uuz-wwx)*factd+fact*(uuzd-wwxd)) - two*&
&             omegayd
            vorty = two*fact*(uuz-wwx) - two*omegay
            vortzd = two*((vvx-uuy)*factd+fact*(vvxd-uuyd)) - two*&
&             omegazd
            vortz = two*fact*(vvx-uuy) - two*omegaz
            if (vortx**2 + vorty**2 + vortz**2 .lt. xminn) then
              max1 = xminn
              max1d = 0.0_8
            else
              max1d = 2*vortx*vortxd + 2*vorty*vortyd + 2*vortz*vortzd
              max1 = vortx**2 + vorty**2 + vortz**2
            end if
            temp10 = sqrt(max1)
            if (max1 .eq. 0.0_8) then
              vortmagd = 0.0_8
            else
              vortmagd = max1d/(2.0*temp10)
            end if
            vortmag = temp10
            sxxd = two*(uux*factd+fact*uuxd)
            sxx = two*fact*uux
            syyd = two*(vvy*factd+fact*vvyd)
            syy = two*fact*vvy
            szzd = two*(wwz*factd+fact*wwzd)
            szz = two*fact*wwz
            sxyd = (uuy+vvx)*factd + fact*(uuyd+vvxd)
            sxy = fact*(uuy+vvx)
            sxzd = (uuz+wwx)*factd + fact*(uuzd+wwxd)
            sxz = fact*(uuz+wwx)
            syzd = (vvz+wwy)*factd + fact*(vvzd+wwyd)
            syz = fact*(vvz+wwy)
            strainmag2d = two*(2*sxy*sxyd+2*sxz*sxzd+2*syz*syzd) + 2*sxx&
&             *sxxd + 2*syy*syyd + 2*szz*szzd
            strainmag2 = two*(sxy**2+sxz**2+syz**2) + sxx**2 + syy**2 + &
&             szz**2
            if (two*strainmag2 .lt. xminn) then
              max2 = xminn
              max2d = 0.0_8
            else
              max2d = two*strainmag2d
              max2 = two*strainmag2
            end if
            temp10 = sqrt(max2)
            if (max2 .eq. 0.0_8) then
              strainmagd = 0.0_8
            else
              strainmagd = max2d/(2.0*temp10)
            end if
            strainmag = temp10
! --- local variables ---
!v_t= ν̃ · fv1 is the sa eddy viscosity
            temp10 = w(i, j, k, itu1)
            nutsad = fv1*wd(i, j, k, itu1) + temp10*fv1d
            nutsa = temp10*fv1
!rturb= ν_t/ν
            rturbd = (nutsad-nutsa*nud/nu)/nu
            rturb = nutsa/nu
            if (w(i, j, k, itu2) .lt. rsagrgammalo) then
              x2 = rsagrgammalo
              x2d = 0.0_8
            else
              x2d = wd(i, j, k, itu2)
              x2 = w(i, j, k, itu2)
            end if
            if (x2 .gt. rsagrgammahi) then
              gammalocal = rsagrgammahi
              gammalocald = 0.0_8
            else
              gammalocald = x2d
              gammalocal = x2
            end if
            if (w(i, j, k, itu3) .lt. rsagrrethetalo) then
              rethetatilde = rsagrrethetalo
              rethetatilded = 0.0_8
            else
              rethetatilded = wd(i, j, k, itu3)
              rethetatilde = w(i, j, k, itu3)
            end if
            ydistd = d2walld(i, j, k)
            ydist = d2wall(i, j, k)
            temp10 = w(i, j, k, ivx)
            temp9 = w(i, j, k, ivy)
            temp8 = w(i, j, k, ivz)
            velmag2d = 2*temp10*wd(i, j, k, ivx) + 2*temp9*wd(i, j, k, &
&             ivy) + 2*temp8*wd(i, j, k, ivz)
            velmag2 = temp10*temp10 + temp9*temp9 + temp8*temp8
            if (velmag2 .lt. xminn) then
              max3 = xminn
              max3d = 0.0_8
            else
              max3d = velmag2d
              max3 = velmag2
            end if
            temp10 = sqrt(max3)
            if (max3 .eq. 0.0_8) then
              velmagd = 0.0_8
            else
              velmagd = max3d/(2.0*temp10)
            end if
            velmag = temp10
            if (muinf .lt. xminn) then
              max12 = xminn
            else
              max12 = muinf
            end if
            x3 = uinf/max12
            if (x3 .lt. xminn) then
              max4 = xminn
            else
              max4 = x3
            end if
! --- vorticity limiting ---
! adflow nondim of paper eqs. 52–53. paper writes m·√(m·re)/20
! using a∞ as velocity scale; adflow uses √(p/ρ) as velocity
! scale and √(p*ρ) for dynamic viscosity.
! rotating frame not adress here!!!!! uinf has no meaning on it.
            result1 = sqrt(max4)
            vortlim = uinf*result1/20.0_realtype
            vortlimd = 0.0_8
            vortmaglimd = smoothminmax_d(vortmag, vortmagd, vortlim, &
&             vortlimd, rsagrpmin, vortmaglim)
! --- fonset (smooth tanh-based transition onset) ---
            temp10 = ydist*ydist*strainmag
            temp9 = w(i, j, k, irho)/rlv(i, j, k)
            res_vald = temp10*(wd(i, j, k, irho)-temp9*rlvd(i, j, k))/&
&             rlv(i, j, k) + temp9*(strainmag*2*ydist*ydistd+ydist**2*&
&             strainmagd)
            res_val = temp9*temp10
            rethetac_vald = rethetaccorrelation_d(rethetatilde, &
&             rethetatilded, rethetac_val)
            temp10 = res_val/(2.6_realtype*rethetac_val)
            arg1d = 2*temp10*(res_vald-temp10*2.6_realtype*rethetac_vald&
&             )/(2.6_realtype*rethetac_val) + 2*rturb*rturbd
            arg1 = temp10*temp10 + rturb*rturb
            temp11 = sqrt(arg1)
            if (arg1 .eq. 0.0_8) then
              fonset1d = 0.0_8
            else
              fonset1d = arg1d/(2.0*temp11)
            end if
            fonset1 = temp11
            fonsetd = half*(1.0-tanh(6.0_realtype*(fonset1-1.35_realtype&
&             ))**2)*6.0_realtype*fonset1d
            fonset = (tanh(6.0_realtype*(fonset1-1.35_realtype))+one)*&
&             half
! --- flength and fturb (modified) ---
            flength_vald = flengthcorrelation_d(rethetatilde, &
&             rethetatilded, flength_val)
            temp10 = exp(-rturb)
            fturb_vald = -(temp10*fonsetd) - (one-fonset)*exp(-rturb)*&
&             rturbd
            fturb_val = (one-fonset)*temp10
!check here if needed 
!fturb_val = exp(-(rturb / 4.0_realtype)**4)
! --- gamma production and destruction ---
            temp10 = sqrt(gammalocal)
            if (gammalocal .eq. 0.0_8) then
              result1d = 0.0_8
            else
              result1d = gammalocald/(2.0*temp10)
            end if
            result1 = temp10
            temp10 = flength_val*fonset*vortmaglim*result1
            pgammad = rsagrca1*((one-rsagrce1*gammalocal)*(vortmaglim*&
&             result1*(fonset*flength_vald+flength_val*fonsetd)+&
&             flength_val*fonset*(result1*vortmaglimd+vortmaglim*&
&             result1d))-temp10*rsagrce1*gammalocald)
            pgamma = rsagrca1*(temp10*(one-rsagrce1*gammalocal))
            temp10 = fturb_val*vortmaglim*gammalocal
            egammad = rsagrca2*((rsagrce2*gammalocal-one)*(gammalocal*(&
&             vortmaglim*fturb_vald+fturb_val*vortmaglimd)+fturb_val*&
&             vortmaglim*gammalocald)+temp10*rsagrce2*gammalocald)
            egamma = rsagrca2*(temp10*(rsagrce2*gammalocal-one))
            scratchd(i, j, k, idvt+1) = pgammad - egammad
            scratch(i, j, k, idvt+1) = pgamma - egamma
            if (velmag2 .lt. xminn) then
              max5 = xminn
              max5d = 0.0_8
            else
              max5d = velmag2d
              max5 = velmag2
            end if
! --- retheta production (relaxation toward correlation) ---
! note: no explicit reynolds factor here.
! nondim form: rlv = mu/muref with l_ref=1m so re=1 implicitly
! (see initializeflow.f90:62-66). no explicit re factor needed,
! consistent with nu = rlv/rho in sa.f90:245.
            timescaled = 500.0_realtype*(nud-nu*max5d/max5)/max5
            timescale = 500.0_realtype*nu/max5
            if (velmag .lt. xminn) then
              max6 = xminn
              max6d = 0.0_8
            else
              max6d = velmagd
              max6 = velmag
            end if
! compute thetabl first (needed for lambdatheta)
            temp10 = rethetatilde*nu/max6
            thetabld = (nu*rethetatilded+rethetatilde*nud-temp10*max6d)/&
&             max6
            thetabl = temp10
            if (velmag .lt. xminn) then
              max7 = xminn
              max7d = 0.0_8
            else
              max7d = velmagd
              max7 = velmag
            end if
! compute local lambdatheta = (thetabl^2 / nu) * du/ds
            temp10 = w(i, j, k, ivx)/max7
            uxhatd = (wd(i, j, k, ivx)-temp10*max7d)/max7
            uxhat = temp10
            if (velmag .lt. xminn) then
              max8 = xminn
              max8d = 0.0_8
            else
              max8d = velmagd
              max8 = velmag
            end if
            temp10 = w(i, j, k, ivy)/max8
            uyhatd = (wd(i, j, k, ivy)-temp10*max8d)/max8
            uyhat = temp10
            if (velmag .lt. xminn) then
              max9 = xminn
              max9d = 0.0_8
            else
              max9d = velmagd
              max9 = velmag
            end if
            temp10 = w(i, j, k, ivz)/max9
            uzhatd = (wd(i, j, k, ivz)-temp10*max9d)/max9
            uzhat = temp10
            temp10 = uxhat*wwx + uyhat*wwy + uzhat*wwz
            temp9 = uxhat*vvx + uyhat*vvy + uzhat*vvz
            temp8 = uxhat*uux + uyhat*uuy + uzhat*uuz
            temp7 = uxhat*temp8 + uyhat*temp9 + uzhat*temp10
            dudsd = two*(temp7*factd+fact*(temp8*uxhatd+uxhat*(uux*&
&             uxhatd+uxhat*uuxd+uuy*uyhatd+uyhat*uuyd+uuz*uzhatd+uzhat*&
&             uuzd)+temp9*uyhatd+uyhat*(vvx*uxhatd+uxhat*vvxd+vvy*uyhatd&
&             +uyhat*vvyd+vvz*uzhatd+uzhat*vvzd)+temp10*uzhatd+uzhat*(&
&             wwx*uxhatd+uxhat*wwxd+wwy*uyhatd+uyhat*wwyd+wwz*uzhatd+&
&             uzhat*wwzd)))
            duds = two*(fact*temp7)
            temp10 = duds/nu
            lambdathetalocald = temp10*2*thetabl*thetabld + thetabl**2*(&
&             dudsd-temp10*nud)/nu
            lambdathetalocal = thetabl*thetabl*temp10
            lambdathetalocald = smoothminmax_d(lambdathetalocal, &
&             lambdathetalocald, -0.1_realtype, 0.0_8, rsagrpmax, &
&             tmpresult)
            lambdathetalocal = tmpresult
            lambdathetalocald = smoothminmax_d(lambdathetalocal, &
&             lambdathetalocald, 0.1_realtype, 0.0_8, rsagrpmin, &
&             tmpresult0)
            lambdathetalocal = tmpresult0
            rethetat_targetd = rethetatcorrelation_d(turbintensityinf*&
&             100.0_realtype, lambdathetalocal, lambdathetalocald, &
&             rethetat_target)
! ftheta_t shielding: shields bl interior, allows
! freestream to drive retheta toward correlation value
            deltabld = 7.5_realtype*thetabld
            deltabl = 7.5_realtype*thetabl
            if (velmag .lt. xminn) then
              max10 = xminn
              max10d = 0.0_8
            else
              max10d = velmagd
              max10 = velmag
            end if
            temp10 = deltabl/max10
            deltad = 50.0_realtype*(temp10*(vortmag*ydistd+ydist*&
&             vortmagd)+ydist*vortmag*(deltabld-temp10*max10d)/max10)
            delta = 50.0_realtype*(ydist*vortmag*temp10)
            if (delta .lt. xminn) then
              delta = xminn
              deltad = 0.0_8
            else
              delta = delta
            end if
            fwake_vald = -(exp(-(res_val/1.0e6_realtype))*res_vald/&
&             1.0e6_realtype)
            fwake_val = exp(-(res_val/1.0e6_realtype))
            temp10 = ydist/delta
            arg1d = -(4*temp10**3*(ydistd-temp10*deltad)/delta)
            arg1 = -(temp10**4)
            temp10 = exp(arg1)
            fthetatd = temp10*fwake_vald + fwake_val*exp(arg1)*arg1d
            fthetat = fwake_val*temp10
            if (timescale .lt. xminn) then
              max11 = xminn
              max11d = 0.0_8
            else
              max11d = timescaled
              max11 = timescale
            end if
            temp10 = (rethetat_target-rethetatilde)/max11
            prethetad = rsagrcthetat*((one-fthetat)*(rethetat_targetd-&
&             rethetatilded-temp10*max11d)/max11-temp10*fthetatd)
            pretheta = rsagrcthetat*(temp10*(one-fthetat))
            scratchd(i, j, k, idvt+2) = prethetad
            scratch(i, j, k, idvt+2) = pretheta
            if (associated(transitiondebug)) then
              dudx = two*fact*uux
              dudy = two*fact*uuy
              dudz = two*fact*uuz
              dvdx = two*fact*vvx
              dvdy = two*fact*vvy
              dvdz = two*fact*vvz
              dwdx = two*fact*wwx
              dwdy = two*fact*wwy
              dwdz = two*fact*wwz
              transitiondebug(i, j, k, dbgfonset) = fonset
              transitiondebug(i, j, k, dbgfonset1) = fonset1
              transitiondebug(i, j, k, dbgflength) = flength_val
              transitiondebug(i, j, k, dbgrturb) = rturb
              transitiondebug(i, j, k, dbgrethetatarget) = &
&               rethetat_target
              transitiondebug(i, j, k, dbgres) = res_val
              transitiondebug(i, j, k, dbgrethetac) = rethetac_val
              transitiondebug(i, j, k, dbgresovercrit) = res_val/(&
&               2.6_realtype*rethetac_val)
              transitiondebug(i, j, k, dbgstrainmag) = strainmag
              transitiondebug(i, j, k, dbgfthetat) = fthetat
              transitiondebug(i, j, k, dbgfwake) = fwake_val
              transitiondebug(i, j, k, dbgdudx) = dudx
              transitiondebug(i, j, k, dbgdudy) = dudy
              transitiondebug(i, j, k, dbgdudz) = dudz
              transitiondebug(i, j, k, dbgdvdx) = dvdx
              transitiondebug(i, j, k, dbgdvdy) = dvdy
              transitiondebug(i, j, k, dbgdvdz) = dvdz
              transitiondebug(i, j, k, dbgdwdx) = dwdx
              transitiondebug(i, j, k, dbgdwdy) = dwdy
              transitiondebug(i, j, k, dbgdwdz) = dwdz
              transitiondebug(i, j, k, dbggamma) = w(i, j, k, itu2)
              transitiondebug(i, j, k, dbgwalldist) = ydist
              transitiondebug(i, j, k, dbgrho) = w(i, j, k, irho)
              transitiondebug(i, j, k, dbgmu) = rlv(i, j, k)
              transitiondebug(i, j, k, dbggammaprod) = pgamma
              transitiondebug(i, j, k, dbggammadest) = egamma
              transitiondebug(i, j, k, dbgtimescale) = timescale
              transitiondebug(i, j, k, dbglambdatheta) = &
&               lambdathetalocal
              transitiondebug(i, j, k, dbgpretheta) = pretheta
              transitiondebug(i, j, k, dbggammaforsa) = gammaforsa
              transitiondebug(i, j, k, dbggammalocal) = gammalocal
              transitiondebug(i, j, k, dbgvortmag) = vortmag
              transitiondebug(i, j, k, dbgvortmaglim) = vortmaglim
              transitiondebug(i, j, k, dbgfturb) = fturb_val
              transitiondebug(i, j, k, dbgss) = ss
              transitiondebug(i, j, k, dbgsst) = sst
              transitiondebug(i, j, k, dbgft2) = ft2
              transitiondebug(i, j, k, dbgthetabl) = thetabl
              transitiondebug(i, j, k, dbgdeltabl) = deltabl
              transitiondebug(i, j, k, dbgdelta) = delta
              transitiondebug(i, j, k, dbgvelmag) = velmag
              transitiondebug(i, j, k, dbgduds) = duds
              transitiondebug(i, j, k, dbgnutsa) = nutsa
              transitiondebug(i, j, k, dbgrethetatilde) = rethetatilde
              transitiondebug(i, j, k, dbgvortlim) = vortlim
            end if
          end do
        end do
      end do
    end if
  end subroutine source_d

  subroutine source()
!
!  source terms.
!  determine the source term and its derivative w.r.t. nutilde
!  for all internal cells of the block.
!  remember that the sa field variable nutilde = w(i,j,k,itu1)
    use blockpointers
    use constants
    use paramturb
    use section
    use inputphysics
    use turbmod, only : dvt, vort, prod, kwcd, f1
    use inputdiscretization, only : approxsa
    use flowvarrefstate
    use turbutils_d, only : rethetatcorrelation, flengthcorrelation, &
&   rethetaccorrelation, smoothminmax
    use inputiteration, only : transitionsrcdtrestrict
    implicit none
! local parameters
    real(kind=realtype), parameter :: f23=two*third
    integer(kind=inttype), parameter :: dbgfonset=1_inttype
    integer(kind=inttype), parameter :: dbgfonset1=2_inttype
    integer(kind=inttype), parameter :: dbgflength=3_inttype
    integer(kind=inttype), parameter :: dbgrturb=4_inttype
    integer(kind=inttype), parameter :: dbgrethetatarget=5_inttype
    integer(kind=inttype), parameter :: dbgres=6_inttype
    integer(kind=inttype), parameter :: dbgrethetac=7_inttype
    integer(kind=inttype), parameter :: dbgresovercrit=8_inttype
    integer(kind=inttype), parameter :: dbgstrainmag=9_inttype
    integer(kind=inttype), parameter :: dbgfthetat=10_inttype
    integer(kind=inttype), parameter :: dbgfwake=11_inttype
    integer(kind=inttype), parameter :: dbgdudx=12_inttype
    integer(kind=inttype), parameter :: dbgdudy=13_inttype
    integer(kind=inttype), parameter :: dbgdudz=14_inttype
    integer(kind=inttype), parameter :: dbgdvdx=15_inttype
    integer(kind=inttype), parameter :: dbgdvdy=16_inttype
    integer(kind=inttype), parameter :: dbgdvdz=17_inttype
    integer(kind=inttype), parameter :: dbgdwdx=18_inttype
    integer(kind=inttype), parameter :: dbgdwdy=19_inttype
    integer(kind=inttype), parameter :: dbgdwdz=20_inttype
    integer(kind=inttype), parameter :: dbggamma=21_inttype
    integer(kind=inttype), parameter :: dbgwalldist=22_inttype
    integer(kind=inttype), parameter :: dbgrho=23_inttype
    integer(kind=inttype), parameter :: dbgmu=24_inttype
    integer(kind=inttype), parameter :: dbggammaprod=25_inttype
    integer(kind=inttype), parameter :: dbggammadest=26_inttype
    integer(kind=inttype), parameter :: dbgtimescale=27_inttype
    integer(kind=inttype), parameter :: dbglambdatheta=28_inttype
    integer(kind=inttype), parameter :: dbgpretheta=29_inttype
    integer(kind=inttype), parameter :: dbggammaforsa=30_inttype
    integer(kind=inttype), parameter :: dbggammalocal=31_inttype
    integer(kind=inttype), parameter :: dbgvortmag=32_inttype
    integer(kind=inttype), parameter :: dbgvortmaglim=33_inttype
    integer(kind=inttype), parameter :: dbgfturb=34_inttype
    integer(kind=inttype), parameter :: dbgss=35_inttype
    integer(kind=inttype), parameter :: dbgsst=36_inttype
    integer(kind=inttype), parameter :: dbgft2=37_inttype
    integer(kind=inttype), parameter :: dbgthetabl=38_inttype
    integer(kind=inttype), parameter :: dbgdeltabl=39_inttype
    integer(kind=inttype), parameter :: dbgdelta=40_inttype
    integer(kind=inttype), parameter :: dbgvelmag=41_inttype
    integer(kind=inttype), parameter :: dbgduds=42_inttype
    integer(kind=inttype), parameter :: dbgnutsa=43_inttype
    integer(kind=inttype), parameter :: dbgrethetatilde=44_inttype
    integer(kind=inttype), parameter :: dbgvortlim=45_inttype
    integer(kind=inttype), parameter :: dbgqq11=46_inttype
    integer(kind=inttype), parameter :: dbgqq22=47_inttype
    integer(kind=inttype), parameter :: dbgqq33=48_inttype
! local variables.
    integer(kind=inttype) :: i, j, k, nn, ii
    real(kind=realtype) :: fv1, fv2, ft2
    real(kind=realtype) :: ss, sst, nu, dist2inv, chi, chi2, chi3
    real(kind=realtype) :: rr, gg, gg6, termfw, fwsa, term1, term2
    real(kind=realtype) :: term2_prod, term2_dest
    real(kind=realtype) :: cv13, kar2inv, cw36, cb3inv
    real(kind=realtype), parameter :: gammastatic=one
    real(kind=realtype), parameter :: rethetastatic=one
    real(kind=realtype) :: dfv1, dfv2, dft2, drr, dgg, dfw
    real(kind=realtype) :: uux, uuy, uuz, vvx, vvy, vvz, wwx, wwy, wwz
    real(kind=realtype) :: div2, fact, sxx, syy, szz, sxy, sxz, syz
    real(kind=realtype) :: vortx, vorty, vortz
    real(kind=realtype) :: omegax, omegay, omegaz
    real(kind=realtype) :: strainmag2, strainprod, vortprod
    real(kind=realtype), parameter :: xminn=1.e-10_realtype
! gamma-retheta source term variables
    real(kind=realtype) :: vortmag, strainmag
    real(kind=realtype) :: nutsa, rturb, gammalocal, rethetatilde
    real(kind=realtype) :: gammaforsa
    real(kind=realtype) :: res_val, rethetac_val, flength_val, fturb_val
    real(kind=realtype) :: fonset, fonset1
    real(kind=realtype) :: vortlim, vortmaglim
    real(kind=realtype) :: pgamma, egamma
    real(kind=realtype) :: velmag, velmag2, timescale, rethetat_target
    real(kind=realtype) :: thetabl, deltabl, delta, fwake_val, fthetat
    real(kind=realtype) :: gammaeff, gammaterm
    real(kind=realtype) :: pretheta, ydist
    real(kind=realtype) :: uxhat, uyhat, uzhat, duds, lambdathetalocal
    real(kind=realtype) :: dudx, dudy, dudz, dvdx, dvdy, dvdz
    real(kind=realtype) :: dwdx, dwdy, dwdz
    real(kind=realtype) :: epsrt, rethetatilde_p, rethetac_p
    real(kind=realtype) :: fonset1_p, fonset_p, flength_p, pgamma_p
    real(kind=realtype) :: drturb_dnu, dfturb_dnu, dfonset_dnu
    real(kind=realtype) :: dfonset1_drt, dfonset_dfonset1
    intrinsic sqrt
    intrinsic exp
    intrinsic min
    intrinsic max
    intrinsic tanh
    intrinsic associated
    real(kind=realtype) :: y1
    real(kind=realtype) :: x1
    real(kind=realtype) :: x2
    real(kind=realtype) :: x3
    real(kind=realtype) :: min1
    real(kind=realtype) :: max1
    real(kind=realtype) :: max2
    real(kind=realtype) :: max3
    real(kind=realtype) :: max4
    real(kind=realtype) :: max5
    real(kind=realtype) :: max6
    real(kind=realtype) :: max7
    real(kind=realtype) :: max8
    real(kind=realtype) :: max9
    real(kind=realtype) :: max10
    real(kind=realtype) :: max11
    real(kind=realtype) :: max12
    real(kind=realtype) :: result1
    real(kind=realtype) :: arg1
! set model constants
    cv13 = rsacv1**3
    kar2inv = one/rsak**2
    cw36 = rsacw3**6
    cb3inv = one/rsacb3
! determine the non-dimensional wheel speed of this block.
    omegax = timeref*sections(sectionid)%rotrate(1)
    omegay = timeref*sections(sectionid)%rotrate(2)
    omegaz = timeref*sections(sectionid)%rotrate(3)
! create switches to production term depending on the variable that
! should be used
    if (turbprod .eq. katolaunder) then
      print*, 'katolaunder production term not supported for sa'
      stop
    else
      do k=2,kl
        do j=2,jl
          do i=2,il
! compute the gradient of u in the cell center. use is made
! of the fact that the surrounding normals sum up to zero,
! such that the cell i,j,k does not give a contribution.
! the gradient is scaled by the factor 2*vol.
            uux = w(i+1, j, k, ivx)*si(i, j, k, 1) - w(i-1, j, k, ivx)*&
&             si(i-1, j, k, 1) + w(i, j+1, k, ivx)*sj(i, j, k, 1) - w(i&
&             , j-1, k, ivx)*sj(i, j-1, k, 1) + w(i, j, k+1, ivx)*sk(i, &
&             j, k, 1) - w(i, j, k-1, ivx)*sk(i, j, k-1, 1)
            uuy = w(i+1, j, k, ivx)*si(i, j, k, 2) - w(i-1, j, k, ivx)*&
&             si(i-1, j, k, 2) + w(i, j+1, k, ivx)*sj(i, j, k, 2) - w(i&
&             , j-1, k, ivx)*sj(i, j-1, k, 2) + w(i, j, k+1, ivx)*sk(i, &
&             j, k, 2) - w(i, j, k-1, ivx)*sk(i, j, k-1, 2)
            uuz = w(i+1, j, k, ivx)*si(i, j, k, 3) - w(i-1, j, k, ivx)*&
&             si(i-1, j, k, 3) + w(i, j+1, k, ivx)*sj(i, j, k, 3) - w(i&
&             , j-1, k, ivx)*sj(i, j-1, k, 3) + w(i, j, k+1, ivx)*sk(i, &
&             j, k, 3) - w(i, j, k-1, ivx)*sk(i, j, k-1, 3)
! idem for the gradient of v.
            vvx = w(i+1, j, k, ivy)*si(i, j, k, 1) - w(i-1, j, k, ivy)*&
&             si(i-1, j, k, 1) + w(i, j+1, k, ivy)*sj(i, j, k, 1) - w(i&
&             , j-1, k, ivy)*sj(i, j-1, k, 1) + w(i, j, k+1, ivy)*sk(i, &
&             j, k, 1) - w(i, j, k-1, ivy)*sk(i, j, k-1, 1)
            vvy = w(i+1, j, k, ivy)*si(i, j, k, 2) - w(i-1, j, k, ivy)*&
&             si(i-1, j, k, 2) + w(i, j+1, k, ivy)*sj(i, j, k, 2) - w(i&
&             , j-1, k, ivy)*sj(i, j-1, k, 2) + w(i, j, k+1, ivy)*sk(i, &
&             j, k, 2) - w(i, j, k-1, ivy)*sk(i, j, k-1, 2)
            vvz = w(i+1, j, k, ivy)*si(i, j, k, 3) - w(i-1, j, k, ivy)*&
&             si(i-1, j, k, 3) + w(i, j+1, k, ivy)*sj(i, j, k, 3) - w(i&
&             , j-1, k, ivy)*sj(i, j-1, k, 3) + w(i, j, k+1, ivy)*sk(i, &
&             j, k, 3) - w(i, j, k-1, ivy)*sk(i, j, k-1, 3)
! and for the gradient of w.
            wwx = w(i+1, j, k, ivz)*si(i, j, k, 1) - w(i-1, j, k, ivz)*&
&             si(i-1, j, k, 1) + w(i, j+1, k, ivz)*sj(i, j, k, 1) - w(i&
&             , j-1, k, ivz)*sj(i, j-1, k, 1) + w(i, j, k+1, ivz)*sk(i, &
&             j, k, 1) - w(i, j, k-1, ivz)*sk(i, j, k-1, 1)
            wwy = w(i+1, j, k, ivz)*si(i, j, k, 2) - w(i-1, j, k, ivz)*&
&             si(i-1, j, k, 2) + w(i, j+1, k, ivz)*sj(i, j, k, 2) - w(i&
&             , j-1, k, ivz)*sj(i, j-1, k, 2) + w(i, j, k+1, ivz)*sk(i, &
&             j, k, 2) - w(i, j, k-1, ivz)*sk(i, j, k-1, 2)
            wwz = w(i+1, j, k, ivz)*si(i, j, k, 3) - w(i-1, j, k, ivz)*&
&             si(i-1, j, k, 3) + w(i, j+1, k, ivz)*sj(i, j, k, 3) - w(i&
&             , j-1, k, ivz)*sj(i, j-1, k, 3) + w(i, j, k+1, ivz)*sk(i, &
&             j, k, 3) - w(i, j, k-1, ivz)*sk(i, j, k-1, 3)
! compute the components of the stress tensor.
! the combination of the current scaling of the velocity
! gradients (2*vol) and the definition of the stress tensor,
! leads to the factor 1/(4*vol).
            fact = fourth/vol(i, j, k)
            if (turbprod .eq. strain) then
              sxx = two*fact*uux
              syy = two*fact*vvy
              szz = two*fact*wwz
              sxy = fact*(uuy+vvx)
              sxz = fact*(uuz+wwx)
              syz = fact*(vvz+wwy)
! compute 2/3 * divergence of velocity squared
              div2 = f23*(sxx+syy+szz)**2
! compute strain production term
              strainmag2 = two*(sxy**2+sxz**2+syz**2) + sxx**2 + syy**2 &
&               + szz**2
              strainprod = two*strainmag2 - div2
              ss = sqrt(strainprod)
            else if (turbprod .eq. vorticity) then
! compute the three components of the vorticity vector.
! substract the part coming from the rotating frame.
              vortx = two*fact*(wwy-vvz) - two*omegax
              vorty = two*fact*(uuz-wwx) - two*omegay
              vortz = two*fact*(vvx-uuy) - two*omegaz
! compute the vorticity production term
              vortprod = vortx**2 + vorty**2 + vortz**2
! first take the square root of the production term to
! obtain the correct production term for spalart-allmaras.
! we do this to avoid if statements.
              ss = sqrt(vortprod)
            end if
! compute the laminar kinematic viscosity, the inverse of
! wall distance squared, the ratio chi (ratio of nutilde
! and nu) and the functions fv1 and fv2. the latter corrects
! the production term near a viscous wall.
            nu = rlv(i, j, k)/w(i, j, k, irho)
            dist2inv = one/d2wall(i, j, k)**2
            chi = w(i, j, k, itu1)/nu
            chi2 = chi*chi
            chi3 = chi*chi2
            fv1 = chi3/(chi3+cv13)
            fv2 = one - chi/(one+chi*fv1)
! the function ft2, which is designed to keep a laminar
! solution laminar. when running in fully turbulent mode
! this function should be set to 0.0.
            if (useft2sa) then
              ft2 = rsact3*exp(-(rsact4*chi2))
            else
              ft2 = zero
            end if
! correct the production term to account for the influence
! of the wall.
            sst = ss + w(i, j, k, itu1)*fv2*kar2inv*dist2inv
! add rotation term (userotationsa defined in inputparams.f90)
            if (userotationsa) then
              y1 = sqrt(two*strainmag2)
              if (zero .gt. y1) then
                min1 = y1
              else
                min1 = zero
              end if
              sst = sst + rsacrot*min1
            end if
            if (sst .lt. xminn) then
              sst = xminn
            else
              sst = sst
            end if
! compute the function fw. the argument rr is cut off at 10
! to avoid numerical problems. this is ok, because the
! asymptotical value of fw is then already reached.
            rr = w(i, j, k, itu1)*kar2inv*dist2inv/sst
            if (rr .gt. 10.0_realtype) then
              rr = 10.0_realtype
            else
              rr = rr
            end if
            gg = rr + rsacw2*(rr**6-rr)
            gg6 = gg**6
            termfw = ((one+cw36)/(gg6+cw36))**sixth
            fwsa = gg*termfw
            if (w(i, j, k, itu2) .lt. zero) then
              x1 = zero
            else
              x1 = w(i, j, k, itu2)
            end if
            if (x1 .gt. one) then
              gammaforsa = one
            else
              gammaforsa = x1
            end if
            if (approxsa) then
              term1 = zero
            else
              term1 = gammaforsa*rsacb1*(one-ft2)*ss
            end if
! split term2 into production and destruction parts.
! production: near-wall correction from cb1*fv2/(kappa^2*d^2)
! destruction: -cw1*fw/d^2
! gamma multiplies only production.
            term2_prod = dist2inv*kar2inv*rsacb1*((one-ft2)*fv2+ft2)
            term2_dest = -(dist2inv*rsacw1*fwsa)
! effective term2 with gamma on production only
            term2 = gammaforsa*term2_prod + term2_dest
            scratch(i, j, k, idvt) = (term1+term2*w(i, j, k, itu1))*w(i&
&             , j, k, itu1)
! ========================================================
! gamma and retheta source terms (slangtry-menter)
! ========================================================
! --- compute vorticity and strain magnitudes ---
            vortx = two*fact*(wwy-vvz) - two*omegax
            vorty = two*fact*(uuz-wwx) - two*omegay
            vortz = two*fact*(vvx-uuy) - two*omegaz
            if (vortx**2 + vorty**2 + vortz**2 .lt. xminn) then
              max1 = xminn
            else
              max1 = vortx**2 + vorty**2 + vortz**2
            end if
            vortmag = sqrt(max1)
            sxx = two*fact*uux
            syy = two*fact*vvy
            szz = two*fact*wwz
            sxy = fact*(uuy+vvx)
            sxz = fact*(uuz+wwx)
            syz = fact*(vvz+wwy)
            strainmag2 = two*(sxy**2+sxz**2+syz**2) + sxx**2 + syy**2 + &
&             szz**2
            if (two*strainmag2 .lt. xminn) then
              max2 = xminn
            else
              max2 = two*strainmag2
            end if
            strainmag = sqrt(max2)
! --- local variables ---
!v_t= ν̃ · fv1 is the sa eddy viscosity
            nutsa = w(i, j, k, itu1)*fv1
!rturb= ν_t/ν
            rturb = nutsa/nu
            if (w(i, j, k, itu2) .lt. rsagrgammalo) then
              x2 = rsagrgammalo
            else
              x2 = w(i, j, k, itu2)
            end if
            if (x2 .gt. rsagrgammahi) then
              gammalocal = rsagrgammahi
            else
              gammalocal = x2
            end if
            if (w(i, j, k, itu3) .lt. rsagrrethetalo) then
              rethetatilde = rsagrrethetalo
            else
              rethetatilde = w(i, j, k, itu3)
            end if
            ydist = d2wall(i, j, k)
            velmag2 = w(i, j, k, ivx)**2 + w(i, j, k, ivy)**2 + w(i, j, &
&             k, ivz)**2
            if (velmag2 .lt. xminn) then
              max3 = xminn
            else
              max3 = velmag2
            end if
            velmag = sqrt(max3)
            if (muinf .lt. xminn) then
              max12 = xminn
            else
              max12 = muinf
            end if
            x3 = uinf/max12
            if (x3 .lt. xminn) then
              max4 = xminn
            else
              max4 = x3
            end if
! --- vorticity limiting ---
! adflow nondim of paper eqs. 52–53. paper writes m·√(m·re)/20
! using a∞ as velocity scale; adflow uses √(p/ρ) as velocity
! scale and √(p*ρ) for dynamic viscosity.
! rotating frame not adress here!!!!! uinf has no meaning on it.
            result1 = sqrt(max4)
            vortlim = uinf*result1/20.0_realtype
            vortmaglim = smoothminmax(vortmag, vortlim, rsagrpmin)
! --- fonset (smooth tanh-based transition onset) ---
            res_val = w(i, j, k, irho)*ydist**2*strainmag/rlv(i, j, k)
            rethetac_val = rethetaccorrelation(rethetatilde)
            arg1 = (res_val/(2.6_realtype*rethetac_val))**2 + rturb**2
            fonset1 = sqrt(arg1)
            fonset = (tanh(6.0_realtype*(fonset1-1.35_realtype))+one)*&
&             half
! --- flength and fturb (modified) ---
            flength_val = flengthcorrelation(rethetatilde)
            fturb_val = (one-fonset)*exp(-rturb)
!check here if needed 
!fturb_val = exp(-(rturb / 4.0_realtype)**4)
! --- gamma production and destruction ---
            result1 = sqrt(gammalocal)
            pgamma = rsagrca1*flength_val*fonset*vortmaglim*result1*(one&
&             -rsagrce1*gammalocal)
            egamma = rsagrca2*fturb_val*vortmaglim*gammalocal*(rsagrce2*&
&             gammalocal-one)
            scratch(i, j, k, idvt+1) = pgamma - egamma
            if (velmag2 .lt. xminn) then
              max5 = xminn
            else
              max5 = velmag2
            end if
! --- retheta production (relaxation toward correlation) ---
! note: no explicit reynolds factor here.
! nondim form: rlv = mu/muref with l_ref=1m so re=1 implicitly
! (see initializeflow.f90:62-66). no explicit re factor needed,
! consistent with nu = rlv/rho in sa.f90:245.
            timescale = 500.0_realtype*nu/max5
            if (velmag .lt. xminn) then
              max6 = xminn
            else
              max6 = velmag
            end if
! compute thetabl first (needed for lambdatheta)
            thetabl = rethetatilde*nu/max6
            if (velmag .lt. xminn) then
              max7 = xminn
            else
              max7 = velmag
            end if
! compute local lambdatheta = (thetabl^2 / nu) * du/ds
            uxhat = w(i, j, k, ivx)/max7
            if (velmag .lt. xminn) then
              max8 = xminn
            else
              max8 = velmag
            end if
            uyhat = w(i, j, k, ivy)/max8
            if (velmag .lt. xminn) then
              max9 = xminn
            else
              max9 = velmag
            end if
            uzhat = w(i, j, k, ivz)/max9
            duds = two*fact*(uxhat*(uxhat*uux+uyhat*uuy+uzhat*uuz)+uyhat&
&             *(uxhat*vvx+uyhat*vvy+uzhat*vvz)+uzhat*(uxhat*wwx+uyhat*&
&             wwy+uzhat*wwz))
            lambdathetalocal = thetabl**2/nu*duds
            lambdathetalocal = smoothminmax(lambdathetalocal, -&
&             0.1_realtype, rsagrpmax)
            lambdathetalocal = smoothminmax(lambdathetalocal, &
&             0.1_realtype, rsagrpmin)
            rethetat_target = rethetatcorrelation(turbintensityinf*&
&             100.0_realtype, lambdathetalocal)
! ftheta_t shielding: shields bl interior, allows
! freestream to drive retheta toward correlation value
            deltabl = 7.5_realtype*thetabl
            if (velmag .lt. xminn) then
              max10 = xminn
            else
              max10 = velmag
            end if
            delta = 50.0_realtype*ydist*vortmag*deltabl/max10
            if (delta .lt. xminn) then
              delta = xminn
            else
              delta = delta
            end if
            fwake_val = exp(-(res_val/1.0e6_realtype))
            arg1 = -((ydist/delta)**4)
            fthetat = fwake_val*exp(arg1)
            if (timescale .lt. xminn) then
              max11 = xminn
            else
              max11 = timescale
            end if
            pretheta = rsagrcthetat/max11*(rethetat_target-rethetatilde)&
&             *(one-fthetat)
            scratch(i, j, k, idvt+2) = pretheta
            if (associated(transitiondebug)) then
              dudx = two*fact*uux
              dudy = two*fact*uuy
              dudz = two*fact*uuz
              dvdx = two*fact*vvx
              dvdy = two*fact*vvy
              dvdz = two*fact*vvz
              dwdx = two*fact*wwx
              dwdy = two*fact*wwy
              dwdz = two*fact*wwz
              transitiondebug(i, j, k, dbgfonset) = fonset
              transitiondebug(i, j, k, dbgfonset1) = fonset1
              transitiondebug(i, j, k, dbgflength) = flength_val
              transitiondebug(i, j, k, dbgrturb) = rturb
              transitiondebug(i, j, k, dbgrethetatarget) = &
&               rethetat_target
              transitiondebug(i, j, k, dbgres) = res_val
              transitiondebug(i, j, k, dbgrethetac) = rethetac_val
              transitiondebug(i, j, k, dbgresovercrit) = res_val/(&
&               2.6_realtype*rethetac_val)
              transitiondebug(i, j, k, dbgstrainmag) = strainmag
              transitiondebug(i, j, k, dbgfthetat) = fthetat
              transitiondebug(i, j, k, dbgfwake) = fwake_val
              transitiondebug(i, j, k, dbgdudx) = dudx
              transitiondebug(i, j, k, dbgdudy) = dudy
              transitiondebug(i, j, k, dbgdudz) = dudz
              transitiondebug(i, j, k, dbgdvdx) = dvdx
              transitiondebug(i, j, k, dbgdvdy) = dvdy
              transitiondebug(i, j, k, dbgdvdz) = dvdz
              transitiondebug(i, j, k, dbgdwdx) = dwdx
              transitiondebug(i, j, k, dbgdwdy) = dwdy
              transitiondebug(i, j, k, dbgdwdz) = dwdz
              transitiondebug(i, j, k, dbggamma) = w(i, j, k, itu2)
              transitiondebug(i, j, k, dbgwalldist) = ydist
              transitiondebug(i, j, k, dbgrho) = w(i, j, k, irho)
              transitiondebug(i, j, k, dbgmu) = rlv(i, j, k)
              transitiondebug(i, j, k, dbggammaprod) = pgamma
              transitiondebug(i, j, k, dbggammadest) = egamma
              transitiondebug(i, j, k, dbgtimescale) = timescale
              transitiondebug(i, j, k, dbglambdatheta) = &
&               lambdathetalocal
              transitiondebug(i, j, k, dbgpretheta) = pretheta
              transitiondebug(i, j, k, dbggammaforsa) = gammaforsa
              transitiondebug(i, j, k, dbggammalocal) = gammalocal
              transitiondebug(i, j, k, dbgvortmag) = vortmag
              transitiondebug(i, j, k, dbgvortmaglim) = vortmaglim
              transitiondebug(i, j, k, dbgfturb) = fturb_val
              transitiondebug(i, j, k, dbgss) = ss
              transitiondebug(i, j, k, dbgsst) = sst
              transitiondebug(i, j, k, dbgft2) = ft2
              transitiondebug(i, j, k, dbgthetabl) = thetabl
              transitiondebug(i, j, k, dbgdeltabl) = deltabl
              transitiondebug(i, j, k, dbgdelta) = delta
              transitiondebug(i, j, k, dbgvelmag) = velmag
              transitiondebug(i, j, k, dbgduds) = duds
              transitiondebug(i, j, k, dbgnutsa) = nutsa
              transitiondebug(i, j, k, dbgrethetatilde) = rethetatilde
              transitiondebug(i, j, k, dbgvortlim) = vortlim
            end if
          end do
        end do
      end do
    end if
  end subroutine source

!  differentiation of viscous in forward (tangent) mode (with options i4 dr8 r8):
!   variations   of useful results: *scratch
!   with respect to varying inputs: *w *rlv *scratch *vol *si *sj
!                *sk
!   rw status of diff variables: *w:in *rlv:in *scratch:in-out
!                *vol:in *si:in *sj:in *sk:in
!   plus diff mem management of: w:in rlv:in scratch:in vol:in
!                si:in sj:in sk:in
  subroutine viscous_d()
!
!  viscous term for sa variable.
!  determine the viscous contribution to the residual
!  for all internal cells of the block.
    use constants
    use blockpointers
    use paramturb
    implicit none
    integer(kind=inttype) :: i, j, k, ii
! viscosity variables
    real(kind=realtype) :: nu, nu_m, nu_p
    real(kind=realtype) :: nud, nu_md, nu_pd
    real(kind=realtype) :: nut, nut_m, nut_p
    real(kind=realtype) :: nutd, nut_md, nut_pd
    real(kind=realtype) :: nu_tm, nu_tp
    real(kind=realtype) :: nu_tmd, nu_tpd
    real(kind=realtype) :: nutilde, nutilde_m, nutilde_p
    real(kind=realtype) :: nutilded, nutilde_md, nutilde_pd
! sa auxiliary functions
    real(kind=realtype) :: chi, chi3
    real(kind=realtype) :: chid, chi3d
    real(kind=realtype) :: chi_m, chi3_m
    real(kind=realtype) :: chi_md, chi3_md
    real(kind=realtype) :: chi_p, chi3_p
    real(kind=realtype) :: chi_pd, chi3_pd
    real(kind=realtype) :: fv1, fv1_m, fv1_p
    real(kind=realtype) :: fv1d, fv1_md, fv1_pd
! geometry terms
    real(kind=realtype) :: voli, volmi, volpi
    real(kind=realtype) :: volid, volmid, volpid
    real(kind=realtype) :: xm, ym, zm
    real(kind=realtype) :: xmd, ymd, zmd
    real(kind=realtype) :: xp, yp, zp
    real(kind=realtype) :: xpd, ypd, zpd
    real(kind=realtype) :: xa, ya, za
    real(kind=realtype) :: xad, yad, zad
    real(kind=realtype) :: ttm, ttp
    real(kind=realtype) :: ttmd, ttpd
! diffusion coefficients
    real(kind=realtype) :: num, nup
    real(kind=realtype) :: numd, nupd
    real(kind=realtype) :: cdm, cdp
    real(kind=realtype) :: cdmd, cdpd
    real(kind=realtype) :: cdm_gamma, cdp_gamma
    real(kind=realtype) :: cdm_gammad, cdp_gammad
    real(kind=realtype) :: cdm_rt, cdp_rt
    real(kind=realtype) :: cdm_rtd, cdp_rtd
! sa nonlinear correction
    real(kind=realtype) :: cnud, cam, cap
    real(kind=realtype) :: cnudd, camd, capd
    real(kind=realtype) :: nutm, nutp
    real(kind=realtype) :: nutmd, nutpd
! matrix coefficients
    real(kind=realtype) :: c1m, c1p, c10
    real(kind=realtype) :: c1md, c1pd, c10d
    real(kind=realtype) :: c2m, c2p, c20
    real(kind=realtype) :: c2md, c2pd, c20d
    real(kind=realtype) :: c3m, c3p, c30
    real(kind=realtype) :: c3md, c3pd, c30d
    real(kind=realtype) :: b1, c1, d1
    real(kind=realtype) :: b2, c2, d2
    real(kind=realtype) :: b3, c3, d3
! constants
    real(kind=realtype) :: cb3inv, cv13
    intrinsic max
    real(kind=realtype) :: temp
    real(kind=realtype) :: temp0
    real(kind=realtype) :: temp1
    cb3inv = one/rsacb3
    cv13 = rsacv1**3
! viscous terms in k-direction.
    do k=2,kl
      do j=2,jl
        do i=2,il
!mesh geometry contribution
          temp = one/vol(i, j, k)
          volid = -(temp*vold(i, j, k)/vol(i, j, k))
          voli = temp
          temp = two/(vol(i, j, k)+vol(i, j, k-1))
          volmid = -(temp*(vold(i, j, k)+vold(i, j, k-1))/(vol(i, j, k)+&
&           vol(i, j, k-1)))
          volmi = temp
          temp = two/(vol(i, j, k)+vol(i, j, k+1))
          volpid = -(temp*(vold(i, j, k)+vold(i, j, k+1))/(vol(i, j, k)+&
&           vol(i, j, k+1)))
          volpi = temp
          temp = sk(i, j, k-1, 1)
          xmd = volmi*skd(i, j, k-1, 1) + temp*volmid
          xm = temp*volmi
          temp = sk(i, j, k-1, 2)
          ymd = volmi*skd(i, j, k-1, 2) + temp*volmid
          ym = temp*volmi
          temp = sk(i, j, k-1, 3)
          zmd = volmi*skd(i, j, k-1, 3) + temp*volmid
          zm = temp*volmi
          temp = sk(i, j, k, 1)
          xpd = volpi*skd(i, j, k, 1) + temp*volpid
          xp = temp*volpi
          temp = sk(i, j, k, 2)
          ypd = volpi*skd(i, j, k, 2) + temp*volpid
          yp = temp*volpi
          temp = sk(i, j, k, 3)
          zpd = volpi*skd(i, j, k, 3) + temp*volpid
          zp = temp*volpi
          temp = sk(i, j, k, 1) + sk(i, j, k-1, 1)
          xad = half*(voli*(skd(i, j, k, 1)+skd(i, j, k-1, 1))+temp*&
&           volid)
          xa = half*(temp*voli)
          temp = sk(i, j, k, 2) + sk(i, j, k-1, 2)
          yad = half*(voli*(skd(i, j, k, 2)+skd(i, j, k-1, 2))+temp*&
&           volid)
          ya = half*(temp*voli)
          temp = sk(i, j, k, 3) + sk(i, j, k-1, 3)
          zad = half*(voli*(skd(i, j, k, 3)+skd(i, j, k-1, 3))+temp*&
&           volid)
          za = half*(temp*voli)
          ttmd = xa*xmd + xm*xad + ya*ymd + ym*yad + za*zmd + zm*zad
          ttm = xm*xa + ym*ya + zm*za
          ttpd = xa*xpd + xp*xad + ya*ypd + yp*yad + za*zpd + zp*zad
          ttp = xp*xa + yp*ya + zp*za
          cnudd = -(rsacb2*cb3inv*wd(i, j, k, itu1))
          cnud = -(rsacb2*w(i, j, k, itu1)*cb3inv)
          camd = cnud*ttmd + ttm*cnudd
          cam = ttm*cnud
          capd = cnud*ttpd + ttp*cnudd
          cap = ttp*cnud
          nutmd = half*(wd(i, j, k-1, itu1)+wd(i, j, k, itu1))
          nutm = half*(w(i, j, k-1, itu1)+w(i, j, k, itu1))
          nutpd = half*(wd(i, j, k+1, itu1)+wd(i, j, k, itu1))
          nutp = half*(w(i, j, k+1, itu1)+w(i, j, k, itu1))
! ----- viscosities in cell (i,j,k)
          temp = w(i, j, k, irho)
          temp0 = rlv(i, j, k)/temp
          nud = (rlvd(i, j, k)-temp0*wd(i, j, k, irho))/temp
          nu = temp0
          nutilded = wd(i, j, k, itu1)
          nutilde = w(i, j, k, itu1)
          chid = (nutilded-nutilde*nud/nu)/nu
          chi = nutilde/nu
          chi3d = 3*chi**2*chid
          chi3 = chi*chi*chi
          temp0 = chi3/(cv13+chi3)
          fv1d = (1.0-temp0)*chi3d/(cv13+chi3)
          fv1 = temp0
          nutd = fv1*nutilded + nutilde*fv1d
          nut = nutilde*fv1
! ----- k-1 cell
          temp0 = w(i, j, k-1, irho)
          temp = rlv(i, j, k-1)/temp0
          nu_md = (rlvd(i, j, k-1)-temp*wd(i, j, k-1, irho))/temp0
          nu_m = temp
          nutilde_md = wd(i, j, k-1, itu1)
          nutilde_m = w(i, j, k-1, itu1)
          chi_md = (nutilde_md-nutilde_m*nu_md/nu_m)/nu_m
          chi_m = nutilde_m/nu_m
          chi3_md = 3*chi_m**2*chi_md
          chi3_m = chi_m*chi_m*chi_m
          temp0 = chi3_m/(cv13+chi3_m)
          fv1_md = (1.0-temp0)*chi3_md/(cv13+chi3_m)
          fv1_m = temp0
          nut_md = fv1_m*nutilde_md + nutilde_m*fv1_md
          nut_m = nutilde_m*fv1_m
! ----- k+1 cell
          temp0 = w(i, j, k+1, irho)
          temp = rlv(i, j, k+1)/temp0
          nu_pd = (rlvd(i, j, k+1)-temp*wd(i, j, k+1, irho))/temp0
          nu_p = temp
          nutilde_pd = wd(i, j, k+1, itu1)
          nutilde_p = w(i, j, k+1, itu1)
          chi_pd = (nutilde_pd-nutilde_p*nu_pd/nu_p)/nu_p
          chi_p = nutilde_p/nu_p
          chi3_pd = 3*chi_p**2*chi_pd
          chi3_p = chi_p*chi_p*chi_p
          temp0 = chi3_p/(cv13+chi3_p)
          fv1_pd = (1.0-temp0)*chi3_pd/(cv13+chi3_p)
          fv1_p = temp0
          nut_pd = fv1_p*nutilde_pd + nutilde_p*fv1_pd
          nut_p = nutilde_p*fv1_p
          numd = half*(nu_md+nud)
          num = half*(nu_m+nu)
          nupd = half*(nu_pd+nud)
          nup = half*(nu_p+nu)
          nu_tmd = half*(nut_md+nutd)
          nu_tm = half*(nut_m+nut)
          nu_tpd = half*(nut_pd+nutd)
          nu_tp = half*(nut_p+nut)
!sa diffusion contribution
          temp0 = num + (one+rsacb2)*nutm
          cdmd = cb3inv*(ttm*(numd+(one+rsacb2)*nutmd)+temp0*ttmd)
          cdm = cb3inv*(temp0*ttm)
          temp0 = nup + (one+rsacb2)*nutp
          cdpd = cb3inv*(ttp*(nupd+(one+rsacb2)*nutpd)+temp0*ttpd)
          cdp = cb3inv*(temp0*ttp)
!gamma diffusion contribution
          temp0 = num + nu_tm/sigmaf
          cdm_gammad = ttm*(numd+nu_tmd/sigmaf) + temp0*ttmd
          cdm_gamma = temp0*ttm
          temp0 = nup + nu_tp/sigmaf
          cdp_gammad = ttp*(nupd+nu_tpd/sigmaf) + temp0*ttpd
          cdp_gamma = temp0*ttp
!rethata diffusion contribution
          cdm_rtd = sigmatheta*(ttm*(numd+nu_tmd)+(num+nu_tm)*ttmd)
          cdm_rt = sigmatheta*(num+nu_tm)*ttm
          cdp_rtd = sigmatheta*(ttp*(nupd+nu_tpd)+(nup+nu_tp)*ttpd)
          cdp_rt = sigmatheta*(nup+nu_tp)*ttp
          if (cdm + cam .lt. zero) then
            c1m = zero
            c1md = 0.0_8
          else
            c1md = cdmd + camd
            c1m = cdm + cam
          end if
          if (cdp + cap .lt. zero) then
            c1p = zero
            c1pd = 0.0_8
          else
            c1pd = cdpd + capd
            c1p = cdp + cap
          end if
          c10d = c1md + c1pd
          c10 = c1m + c1p
          if (cdm_gamma .lt. zero) then
            c2m = zero
            c2md = 0.0_8
          else
            c2md = cdm_gammad
            c2m = cdm_gamma
          end if
          if (cdp_gamma .lt. zero) then
            c2p = zero
            c2pd = 0.0_8
          else
            c2pd = cdp_gammad
            c2p = cdp_gamma
          end if
          c20d = c2md + c2pd
          c20 = c2m + c2p
          if (cdm_rt .lt. zero) then
            c3m = zero
            c3md = 0.0_8
          else
            c3md = cdm_rtd
            c3m = cdm_rt
          end if
          if (cdp_rt .lt. zero) then
            c3p = zero
            c3pd = 0.0_8
          else
            c3pd = cdp_rtd
            c3p = cdp_rt
          end if
          c30d = c3md + c3pd
          c30 = c3m + c3p
          temp0 = w(i, j, k-1, itu1)
          temp = w(i, j, k+1, itu1)
          temp1 = w(i, j, k, itu1)
          scratchd(i, j, k, idvt) = scratchd(i, j, k, idvt) + temp0*c1md&
&           + c1m*wd(i, j, k-1, itu1) + temp*c1pd + c1p*wd(i, j, k+1, &
&           itu1) - temp1*c10d - c10*wd(i, j, k, itu1)
          scratch(i, j, k, idvt) = scratch(i, j, k, idvt) + c1m*temp0 + &
&           c1p*temp - c10*temp1
          temp1 = w(i, j, k-1, itu2)
          temp0 = w(i, j, k+1, itu2)
          temp = w(i, j, k, itu2)
          scratchd(i, j, k, idvt+1) = scratchd(i, j, k, idvt+1) + temp1*&
&           c2md + c2m*wd(i, j, k-1, itu2) + temp0*c2pd + c2p*wd(i, j, k&
&           +1, itu2) - temp*c20d - c20*wd(i, j, k, itu2)
          scratch(i, j, k, idvt+1) = scratch(i, j, k, idvt+1) + c2m*&
&           temp1 + c2p*temp0 - c20*temp
          temp1 = w(i, j, k-1, itu3)
          temp0 = w(i, j, k+1, itu3)
          temp = w(i, j, k, itu3)
          scratchd(i, j, k, idvt+2) = scratchd(i, j, k, idvt+2) + temp1*&
&           c3md + c3m*wd(i, j, k-1, itu3) + temp0*c3pd + c3p*wd(i, j, k&
&           +1, itu3) - temp*c30d - c30*wd(i, j, k, itu3)
          scratch(i, j, k, idvt+2) = scratch(i, j, k, idvt+2) + c3m*&
&           temp1 + c3p*temp0 - c30*temp
        end do
      end do
    end do
! viscous terms in j-direction.
    do k=2,kl
      do j=2,jl
        do i=2,il
          temp1 = one/vol(i, j, k)
          volid = -(temp1*vold(i, j, k)/vol(i, j, k))
          voli = temp1
          temp1 = two/(vol(i, j, k)+vol(i, j-1, k))
          volmid = -(temp1*(vold(i, j, k)+vold(i, j-1, k))/(vol(i, j, k)&
&           +vol(i, j-1, k)))
          volmi = temp1
          temp1 = two/(vol(i, j, k)+vol(i, j+1, k))
          volpid = -(temp1*(vold(i, j, k)+vold(i, j+1, k))/(vol(i, j, k)&
&           +vol(i, j+1, k)))
          volpi = temp1
          temp1 = sj(i, j-1, k, 1)
          xmd = volmi*sjd(i, j-1, k, 1) + temp1*volmid
          xm = temp1*volmi
          temp1 = sj(i, j-1, k, 2)
          ymd = volmi*sjd(i, j-1, k, 2) + temp1*volmid
          ym = temp1*volmi
          temp1 = sj(i, j-1, k, 3)
          zmd = volmi*sjd(i, j-1, k, 3) + temp1*volmid
          zm = temp1*volmi
          temp1 = sj(i, j, k, 1)
          xpd = volpi*sjd(i, j, k, 1) + temp1*volpid
          xp = temp1*volpi
          temp1 = sj(i, j, k, 2)
          ypd = volpi*sjd(i, j, k, 2) + temp1*volpid
          yp = temp1*volpi
          temp1 = sj(i, j, k, 3)
          zpd = volpi*sjd(i, j, k, 3) + temp1*volpid
          zp = temp1*volpi
          temp1 = sj(i, j, k, 1) + sj(i, j-1, k, 1)
          xad = half*(voli*(sjd(i, j, k, 1)+sjd(i, j-1, k, 1))+temp1*&
&           volid)
          xa = half*(temp1*voli)
          temp1 = sj(i, j, k, 2) + sj(i, j-1, k, 2)
          yad = half*(voli*(sjd(i, j, k, 2)+sjd(i, j-1, k, 2))+temp1*&
&           volid)
          ya = half*(temp1*voli)
          temp1 = sj(i, j, k, 3) + sj(i, j-1, k, 3)
          zad = half*(voli*(sjd(i, j, k, 3)+sjd(i, j-1, k, 3))+temp1*&
&           volid)
          za = half*(temp1*voli)
          ttmd = xa*xmd + xm*xad + ya*ymd + ym*yad + za*zmd + zm*zad
          ttm = xm*xa + ym*ya + zm*za
          ttpd = xa*xpd + xp*xad + ya*ypd + yp*yad + za*zpd + zp*zad
          ttp = xp*xa + yp*ya + zp*za
          cnudd = -(rsacb2*cb3inv*wd(i, j, k, itu1))
          cnud = -(rsacb2*w(i, j, k, itu1)*cb3inv)
          camd = cnud*ttmd + ttm*cnudd
          cam = ttm*cnud
          capd = cnud*ttpd + ttp*cnudd
          cap = ttp*cnud
          nutmd = half*(wd(i, j-1, k, itu1)+wd(i, j, k, itu1))
          nutm = half*(w(i, j-1, k, itu1)+w(i, j, k, itu1))
          nutpd = half*(wd(i, j+1, k, itu1)+wd(i, j, k, itu1))
          nutp = half*(w(i, j+1, k, itu1)+w(i, j, k, itu1))
! ----- viscosities in cell (i,j,k)
          temp1 = w(i, j, k, irho)
          temp0 = rlv(i, j, k)/temp1
          nud = (rlvd(i, j, k)-temp0*wd(i, j, k, irho))/temp1
          nu = temp0
          nutilded = wd(i, j, k, itu1)
          nutilde = w(i, j, k, itu1)
          chid = (nutilded-nutilde*nud/nu)/nu
          chi = nutilde/nu
          chi3d = 3*chi**2*chid
          chi3 = chi*chi*chi
          temp1 = chi3/(cv13+chi3)
          fv1d = (1.0-temp1)*chi3d/(cv13+chi3)
          fv1 = temp1
          nutd = fv1*nutilded + nutilde*fv1d
          nut = nutilde*fv1
! ----- j-1 cell
          temp1 = w(i, j-1, k, irho)
          temp0 = rlv(i, j-1, k)/temp1
          nu_md = (rlvd(i, j-1, k)-temp0*wd(i, j-1, k, irho))/temp1
          nu_m = temp0
          nutilde_md = wd(i, j-1, k, itu1)
          nutilde_m = w(i, j-1, k, itu1)
          chi_md = (nutilde_md-nutilde_m*nu_md/nu_m)/nu_m
          chi_m = nutilde_m/nu_m
          chi3_md = 3*chi_m**2*chi_md
          chi3_m = chi_m*chi_m*chi_m
          temp1 = chi3_m/(cv13+chi3_m)
          fv1_md = (1.0-temp1)*chi3_md/(cv13+chi3_m)
