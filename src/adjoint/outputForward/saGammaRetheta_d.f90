&           k-1, itu1) - c10*w(i, j, k, itu1) + c1p*w(i, j, k+1, itu1)
          scratch(i, j, k, idvt+1) = scratch(i, j, k, idvt+1) + c2m*w(i&
&           , j, k-1, itu2) - c20*w(i, j, k, itu2) + c2p*w(i, j, k+1, &
