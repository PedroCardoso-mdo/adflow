          temp1 = nup + nu_tp/sigmaf
          cdp_gammad = ttp*(nupd+nu_tpd/sigmaf) + temp1*ttpd
          cdp_gamma = temp1*ttp
!rethata diffusion contribution
          cdm_rtd = sigmatheta*(ttm*(numd+nu_tmd)+(num+nu_tm)*ttmd)
          cdm_rt = sigmatheta*(num+nu_tm)*ttm
          cdp_rtd = sigmatheta*(ttp*(nupd+nu_tpd)+(nup+nu_tp)*ttpd)
          cdp_rt = sigmatheta*(nup+nu_tp)*ttp
          if (cdm + cam .lt. zero) then
            c1m = zero
            c1md = 0.0_8
          else
            c1md = cdmd + camd
            c1m = cdm + cam
          end if
          if (cdp + cap .lt. zero) then
            c1p = zero
            c1pd = 0.0_8
          else
            c1pd = cdpd + capd
            c1p = cdp + cap
          end if
          c10d = c1md + c1pd
          c10 = c1m + c1p
          if (cdm_gamma .lt. zero) then
            c2m = zero
            c2md = 0.0_8
          else
            c2md = cdm_gammad
            c2m = cdm_gamma
          end if
          if (cdp_gamma .lt. zero) then
            c2p = zero
            c2pd = 0.0_8
          else
            c2pd = cdp_gammad
            c2p = cdp_gamma
          end if
          c20d = c2md + c2pd
          c20 = c2m + c2p
          if (cdm_rt .lt. zero) then
            c3m = zero
            c3md = 0.0_8
          else
            c3md = cdm_rtd
            c3m = cdm_rt
          end if
          if (cdp_rt .lt. zero) then
            c3p = zero
            c3pd = 0.0_8
          else
            c3pd = cdp_rtd
            c3p = cdp_rt
          end if
          c30d = c3md + c3pd
          c30 = c3m + c3p
          temp1 = w(i-1, j, k, itu1)
          temp0 = w(i+1, j, k, itu1)
          temp = w(i, j, k, itu1)
          scratchd(i, j, k, idvt) = scratchd(i, j, k, idvt) + temp1*c1md&
&           + c1m*wd(i-1, j, k, itu1) + temp0*c1pd + c1p*wd(i+1, j, k, &
&           itu1) - temp*c10d - c10*wd(i, j, k, itu1)
          scratch(i, j, k, idvt) = scratch(i, j, k, idvt) + c1m*temp1 + &
&           c1p*temp0 - c10*temp
          temp1 = w(i-1, j, k, itu2)
          temp0 = w(i+1, j, k, itu2)
          temp = w(i, j, k, itu2)
          scratchd(i, j, k, idvt+1) = scratchd(i, j, k, idvt+1) + temp1*&
&           c2md + c2m*wd(i-1, j, k, itu2) + temp0*c2pd + c2p*wd(i+1, j&
&           , k, itu2) - temp*c20d - c20*wd(i, j, k, itu2)
          scratch(i, j, k, idvt+1) = scratch(i, j, k, idvt+1) + c2m*&
&           temp1 + c2p*temp0 - c20*temp
          temp1 = w(i-1, j, k, itu3)
          temp0 = w(i+1, j, k, itu3)
          temp = w(i, j, k, itu3)
          scratchd(i, j, k, idvt+2) = scratchd(i, j, k, idvt+2) + temp1*&
&           c3md + c3m*wd(i-1, j, k, itu3) + temp0*c3pd + c3p*wd(i+1, j&
&           , k, itu3) - temp*c30d - c30*wd(i, j, k, itu3)
          scratch(i, j, k, idvt+2) = scratch(i, j, k, idvt+2) + c3m*&
&           temp1 + c3p*temp0 - c30*temp
        end do
      end do
    end do
  end subroutine viscous_d

  subroutine viscous()
!
!  viscous term for sa variable.
!  determine the viscous contribution to the residual
!  for all internal cells of the block.
    use constants
    use blockpointers
    use paramturb
    implicit none
    integer(kind=inttype) :: i, j, k, ii
! viscosity variables
    real(kind=realtype) :: nu, nu_m, nu_p
    real(kind=realtype) :: nut, nut_m, nut_p
    real(kind=realtype) :: nu_tm, nu_tp
    real(kind=realtype) :: nutilde, nutilde_m, nutilde_p
! sa auxiliary functions
    real(kind=realtype) :: chi, chi3
    real(kind=realtype) :: chi_m, chi3_m
    real(kind=realtype) :: chi_p, chi3_p
    real(kind=realtype) :: fv1, fv1_m, fv1_p
! geometry terms
    real(kind=realtype) :: voli, volmi, volpi
    real(kind=realtype) :: xm, ym, zm
    real(kind=realtype) :: xp, yp, zp
    real(kind=realtype) :: xa, ya, za
    real(kind=realtype) :: ttm, ttp
! diffusion coefficients
    real(kind=realtype) :: num, nup
    real(kind=realtype) :: cdm, cdp
    real(kind=realtype) :: cdm_gamma, cdp_gamma
    real(kind=realtype) :: cdm_rt, cdp_rt
! sa nonlinear correction
    real(kind=realtype) :: cnud, cam, cap
    real(kind=realtype) :: nutm, nutp
! matrix coefficients
    real(kind=realtype) :: c1m, c1p, c10
    real(kind=realtype) :: c2m, c2p, c20
    real(kind=realtype) :: c3m, c3p, c30
    real(kind=realtype) :: b1, c1, d1
    real(kind=realtype) :: b2, c2, d2
    real(kind=realtype) :: b3, c3, d3
! constants
    real(kind=realtype) :: cb3inv, cv13
    intrinsic max
    cb3inv = one/rsacb3
    cv13 = rsacv1**3
! viscous terms in k-direction.
    do k=2,kl
      do j=2,jl
        do i=2,il
!mesh geometry contribution
          voli = one/vol(i, j, k)
          volmi = two/(vol(i, j, k)+vol(i, j, k-1))
          volpi = two/(vol(i, j, k)+vol(i, j, k+1))
          xm = sk(i, j, k-1, 1)*volmi
          ym = sk(i, j, k-1, 2)*volmi
          zm = sk(i, j, k-1, 3)*volmi
          xp = sk(i, j, k, 1)*volpi
          yp = sk(i, j, k, 2)*volpi
          zp = sk(i, j, k, 3)*volpi
