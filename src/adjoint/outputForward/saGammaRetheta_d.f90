          cam = ttm*cnud
          capd = cnud*ttpd + ttp*cnudd
          cap = ttp*cnud
          nutmd = half*(wd(i-1, j, k, itu1)+wd(i, j, k, itu1))
          nutm = half*(w(i-1, j, k, itu1)+w(i, j, k, itu1))
          nutpd = half*(wd(i+1, j, k, itu1)+wd(i, j, k, itu1))
