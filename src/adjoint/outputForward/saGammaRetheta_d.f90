          chi3_p = chi_p*chi_p*chi_p
          temp1 = chi3_p/(cv13+chi3_p)
          fv1_pd = (1.0-temp1)*chi3_pd/(cv13+chi3_p)
          fv1_p = temp1
          nut_pd = fv1_p*nutilde_pd + nutilde_p*fv1_pd
          nut_p = nutilde_p*fv1_p
          numd = half*(nu_md+nud)
          num = half*(nu_m+nu)
          nupd = half*(nu_pd+nud)
          nup = half*(nu_p+nu)
          nu_tmd = half*(nut_md+nutd)
          nu_tm = half*(nut_m+nut)
          nu_tpd = half*(nut_pd+nutd)
          nu_tp = half*(nut_p+nut)
!sa diffusion contribution
          temp1 = num + (one+rsacb2)*nutm
          cdmd = cb3inv*(ttm*(numd+(one+rsacb2)*nutmd)+temp1*ttmd)
          cdm = cb3inv*(temp1*ttm)
          temp1 = nup + (one+rsacb2)*nutp
          cdpd = cb3inv*(ttp*(nupd+(one+rsacb2)*nutpd)+temp1*ttpd)
          cdp = cb3inv*(temp1*ttp)
!gamma diffusion contribution
          temp1 = num + nu_tm/sigmaf
          cdm_gammad = ttm*(numd+nu_tmd/sigmaf) + temp1*ttmd
          cdm_gamma = temp1*ttm
          temp1 = nup + nu_tp/sigmaf
          cdp_gammad = ttp*(nupd+nu_tpd/sigmaf) + temp1*ttpd
          cdp_gamma = temp1*ttp
!rethata diffusion contribution
          cdm_rtd = sigmatheta*(ttm*(numd+nu_tmd)+(num+nu_tm)*ttmd)
          cdm_rt = sigmatheta*(num+nu_tm)*ttm
          cdp_rtd = sigmatheta*(ttp*(nupd+nu_tpd)+(nup+nu_tp)*ttpd)
          cdp_rt = sigmatheta*(nup+nu_tp)*ttp
          if (cdm + cam .lt. zero) then
            c1m = zero
            c1md = 0.0_8
          else
            c1md = cdmd + camd
            c1m = cdm + cam
          end if
          if (cdp + cap .lt. zero) then
            c1p = zero
            c1pd = 0.0_8
          else
            c1pd = cdpd + capd
            c1p = cdp + cap
          end if
          c10d = c1md + c1pd
          c10 = c1m + c1p
          if (cdm_gamma .lt. zero) then
            c2m = zero
            c2md = 0.0_8
          else
            c2md = cdm_gammad
            c2m = cdm_gamma
          end if
          if (cdp_gamma .lt. zero) then
            c2p = zero
            c2pd = 0.0_8
          else
            c2pd = cdp_gammad
            c2p = cdp_gamma
          end if
          c20d = c2md + c2pd
          c20 = c2m + c2p
          if (cdm_rt .lt. zero) then
            c3m = zero
            c3md = 0.0_8
          else
            c3md = cdm_rtd
            c3m = cdm_rt
          end if
          if (cdp_rt .lt. zero) then
            c3p = zero
            c3pd = 0.0_8
          else
            c3pd = cdp_rtd
            c3p = cdp_rt
          end if
          c30d = c3md + c3pd
          c30 = c3m + c3p
          temp1 = w(i, j-1, k, itu1)
          temp0 = w(i, j+1, k, itu1)
          temp = w(i, j, k, itu1)
          scratchd(i, j, k, idvt) = scratchd(i, j, k, idvt) + temp1*c1md&
&           + c1m*wd(i, j-1, k, itu1) + temp0*c1pd + c1p*wd(i, j+1, k, &
&           itu1) - temp*c10d - c10*wd(i, j, k, itu1)
          scratch(i, j, k, idvt) = scratch(i, j, k, idvt) + c1m*temp1 + &
&           c1p*temp0 - c10*temp
          temp1 = w(i, j-1, k, itu2)
          temp0 = w(i, j+1, k, itu2)
          temp = w(i, j, k, itu2)
          scratchd(i, j, k, idvt+1) = scratchd(i, j, k, idvt+1) + temp1*&
&           c2md + c2m*wd(i, j-1, k, itu2) + temp0*c2pd + c2p*wd(i, j+1&
&           , k, itu2) - temp*c20d - c20*wd(i, j, k, itu2)
          scratch(i, j, k, idvt+1) = scratch(i, j, k, idvt+1) + c2m*&
&           temp1 + c2p*temp0 - c20*temp
          temp1 = w(i, j-1, k, itu3)
          temp0 = w(i, j+1, k, itu3)
          temp = w(i, j, k, itu3)
          scratchd(i, j, k, idvt+2) = scratchd(i, j, k, idvt+2) + temp1*&
&           c3md + c3m*wd(i, j-1, k, itu3) + temp0*c3pd + c3p*wd(i, j+1&
&           , k, itu3) - temp*c30d - c30*wd(i, j, k, itu3)
          scratch(i, j, k, idvt+2) = scratch(i, j, k, idvt+2) + c3m*&
&           temp1 + c3p*temp0 - c30*temp
        end do
      end do
    end do
