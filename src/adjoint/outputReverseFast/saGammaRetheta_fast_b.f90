            cc(1, 3, j) = qq(i, j, k, 1, 3)*rblank
            cc(2, 1, j) = qq(i, j, k, 2, 1)*rblank
            cc(2, 2, j) = qq(i, j, k, 2, 2)
            cc(2, 3, j) = qq(i, j, k, 2, 3)*rblank
            cc(3, 1, j) = qq(i, j, k, 3, 1)*rblank
            cc(3, 2, j) = qq(i, j, k, 3, 2)*rblank
            cc(3, 3, j) = qq(i, j, k, 3, 3)
