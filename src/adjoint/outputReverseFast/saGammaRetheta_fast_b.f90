            nu_tm = half*(nut_m+nut)
            nu_tp = half*(nut_p+nut)
! sa diffusion
            cdm = (num_v+(one+rsacb2)*nutm)*ttm*cb3inv
            cdp = (nup_v+(one+rsacb2)*nutp)*ttp*cb3inv
! gamma diffusion
            cdm_gamma = (num_v+nu_tm/sigmaf)*ttm
            cdp_gamma = (nup_v+nu_tp/sigmaf)*ttp
! retheta diffusion
            cdm_rt = sigmatheta*(num_v+nu_tm)*ttm
            cdp_rt = sigmatheta*(nup_v+nu_tp)*ttp
            if (cdm + cam .lt. zero) then
              c1m = zero
            else
              c1m = cdm + cam
            end if
            if (cdp + cap .lt. zero) then
              c1p = zero
            else
              c1p = cdp + cap
            end if
            if (cdm_gamma .lt. zero) then
              c2m = zero
            else
              c2m = cdm_gamma
            end if
            if (cdp_gamma .lt. zero) then
              c2p = zero
            else
              c2p = cdp_gamma
            end if
            if (cdm_rt .lt. zero) then
              c3m = zero
            else
              c3m = cdm_rt
            end if
            if (cdp_rt .lt. zero) then
              c3p = zero
            else
              c3p = cdp_rt
            end if
            bb(1, k) = -c1m
            dd(1, k) = -c1p
            bb(2, k) = -c2m
            dd(2, k) = -c2p
            bb(3, k) = -c3m
            dd(3, k) = -c3p
! add advection off-diagonal terms in k-direction.
            if (addgridvelocities) qs = half*(sfacek(i, j, k)+sfacek(i, &
&               j, k-1))*voli
            uu = xa*w(i, j, k, ivx) + ya*w(i, j, k, ivy) + za*w(i, j, k&
&             , ivz) - qs
            um = zero
            up = zero
            if (uu .lt. zero) um = uu
            if (uu .gt. zero) up = uu
            bb(1, k) = bb(1, k) - up
            dd(1, k) = dd(1, k) + um
            bb(2, k) = bb(2, k) - up
            dd(2, k) = dd(2, k) + um
            bb(3, k) = bb(3, k) - up
            dd(3, k) = dd(3, k) + um
! store central jacobian and rhs in cc and ff.
            rblank = real(iblank(i, j, k), realtype)
            cc(1, 1, k) = qq(i, j, k, 1, 1)
            cc(1, 2, k) = qq(i, j, k, 1, 2)*rblank
            cc(1, 3, k) = qq(i, j, k, 1, 3)*rblank
            cc(2, 1, k) = qq(i, j, k, 2, 1)*rblank
            cc(2, 2, k) = qq(i, j, k, 2, 2)
            cc(2, 3, k) = qq(i, j, k, 2, 3)*rblank
            cc(3, 1, k) = qq(i, j, k, 3, 1)*rblank
            cc(3, 2, k) = qq(i, j, k, 3, 2)*rblank
            cc(3, 3, k) = qq(i, j, k, 3, 3)
            ff(1, k) = scratch(i, j, k, idvt)*rblank
            ff(2, k) = scratch(i, j, k, idvt+1)*rblank
            ff(3, k) = scratch(i, j, k, idvt+2)*rblank
            bb(:, k) = bb(:, k)*rblank
            dd(:, k) = dd(:, k)*rblank
          end do
! solve the tri-diagonal system in k-direction.
          call tdia3x3(2_inttype, kl, bb, cc, dd, ff)
! store the final update in scratch.
          do k=2,kl
            scratch(i, j, k, idvt) = ff(1, k)
            scratch(i, j, k, idvt+1) = ff(2, k)
            scratch(i, j, k, idvt+2) = ff(3, k)
          end do
        end do
      end do
!
!       update the turbulent variables. for explicit relaxation the
!       update must be relaxed; for implicit relaxation this has been
!       done via the time step.
!       unscale the update: δq(m) = s_m * δq_scaled(m).
!
      factor = one
      if (turbrelax .eq. turbrelaxexplicit) factor = alfaturb
      do k=2,kl
        do j=2,jl
          do i=2,il
            w(i, j, k, itu1) = w(i, j, k, itu1) + factor*scalenu*scratch&
&             (i, j, k, idvt)
            if (w(i, j, k, itu1) .lt. zero) then
              w(i, j, k, itu1) = zero
            else
              w(i, j, k, itu1) = w(i, j, k, itu1)
            end if
! gamma update with exponential back-off damping (§3).
! if the raw update overshoots [gammalo, gammahi],
! reduce the step by theta^m until it stays in range.
            gammadelta = factor*scalegamma*scratch(i, j, k, idvt+1)
            gammanew = w(i, j, k, itu2) + gammadelta
            dampfactor = one
            do mm=1,40
              if (gammanew .ge. rsagrgammalo .and. gammanew .le. &
&                 rsagrgammahi) then
                goto 100
              else
                dampfactor = dampfactor*rsagrdamptheta
                gammanew = w(i, j, k, itu2) + dampfactor*gammadelta
              end if
            end do
 100        if (gammanew .lt. rsagrgammalo) then
              x4 = rsagrgammalo
            else
              x4 = gammanew
            end if
            if (x4 .gt. rsagrgammahi) then
              w(i, j, k, itu2) = rsagrgammahi
            else
              w(i, j, k, itu2) = x4
            end if
! retheta update with exponential back-off damping (§3).
            gammadelta = factor*scaleretheta*scratch(i, j, k, idvt+2)
            gammanew = w(i, j, k, itu3) + gammadelta
            dampfactor = one
            do mm=1,40
              if (gammanew .ge. rsagrrethetalo) then
                goto 110
              else
                dampfactor = dampfactor*rsagrdamptheta
                gammanew = w(i, j, k, itu3) + dampfactor*gammadelta
              end if
            end do
 110        if (gammanew .lt. rsagrrethetalo) then
              w(i, j, k, itu3) = rsagrrethetalo
            else
              w(i, j, k, itu3) = gammanew
            end if
          end do
        end do
      end do
    end if
  end subroutine sagammarethetasolve

  subroutine computesrclambda()
! compute srclambda = max(0, λ_max) where λ_max is the largest positive
! eigenvalue of a_source = -qq (p&z 2020 eq. 59).
! note: qq stores -∂s/∂q, so a_source = -qq.
!
! mode 0: signed gershgorin upper bound (ad-safe)
!   srclambda = max(0, max_i[ a_ii + σ_{j≠i} |a_ij| ])
! mode 1: exact 3x3 eigenvalue via cubic formula
    use constants
    use blockpointers, only : il, jl, kl, srclambda
    use inputiteration, only : transitionsrcdteigmode
    implicit none
    integer(kind=inttype) :: i, j, k
    real(kind=realtype) :: a11, a12, a13, a21, a22, a23, a31, a32, a33
    real(kind=realtype) :: g1, g2, g3, lambdamax
    real(kind=realtype) :: p, qc, r, a, b, c, disc, phi, t, sqrtp
    real(kind=realtype), parameter :: onethird=one/three
    real(kind=realtype), parameter :: pival=&
&     3.14159265358979323846_realtype
    intrinsic abs
    intrinsic max
    intrinsic sqrt
    intrinsic min
    intrinsic acos
    intrinsic cos
    intrinsic sign
    real(kind=realtype) :: y1
    real(kind=realtype) :: abs0
    real(kind=realtype) :: abs1
    real(kind=realtype) :: abs2
    real(kind=realtype) :: max1
    real(kind=realtype) :: abs3
    real(kind=realtype) :: abs4
    real(kind=realtype) :: abs5
    real(kind=realtype) :: abs6
    real(kind=realtype) :: abs7
    real(kind=realtype) :: abs8
    real(kind=realtype) :: abs9
    real(kind=realtype) :: abs10
    real(kind=realtype) :: abs11
    real(kind=realtype) :: abs12
    real(kind=realtype) :: abs13
    do k=2,kl
      do j=2,jl
        do i=2,il
! a_source = -qq (qq stores -∂s/∂q)
          a11 = -qq(i, j, k, 1, 1)
          a12 = -qq(i, j, k, 1, 2)
          a13 = -qq(i, j, k, 1, 3)
          a21 = -qq(i, j, k, 2, 1)
          a22 = -qq(i, j, k, 2, 2)
          a23 = -qq(i, j, k, 2, 3)
          a31 = -qq(i, j, k, 3, 1)
          a32 = -qq(i, j, k, 3, 2)
          a33 = -qq(i, j, k, 3, 3)
          if (transitionsrcdteigmode .eq. 0) then
            if (a12 .ge. 0.) then
              abs0 = a12
            else
              abs0 = -a12
            end if
            if (a13 .ge. 0.) then
              abs7 = a13
            else
              abs7 = -a13
            end if
! mode 0: signed gershgorin upper bound
            g1 = a11 + abs0 + abs7
            if (a21 .ge. 0.) then
              abs1 = a21
            else
              abs1 = -a21
            end if
            if (a23 .ge. 0.) then
              abs8 = a23
            else
              abs8 = -a23
            end if
            g2 = a22 + abs1 + abs8
            if (a31 .ge. 0.) then
              abs2 = a31
            else
              abs2 = -a31
            end if
            if (a32 .ge. 0.) then
              abs9 = a32
            else
              abs9 = -a32
            end if
            g3 = a33 + abs2 + abs9
            if (g1 .lt. g2) then
              if (g2 .lt. g3) then
                lambdamax = g3
              else
                lambdamax = g2
              end if
            else if (g1 .lt. g3) then
              lambdamax = g3
            else
              lambdamax = g1
            end if
          else
! mode 1: exact eigenvalues via cubic formula
! characteristic polynomial: λ³ - aλ² + bλ - c = 0
! where a = tr(a), b = (tr(a)² - tr(a²))/2, c = det(a)
            a = a11 + a22 + a33
            b = a11*a22 + a22*a33 + a33*a11 - a12*a21 - a23*a32 - a31*&
&             a13
            c = a11*(a22*a33-a23*a32) - a12*(a21*a33-a23*a31) + a13*(a21&
&             *a32-a22*a31)
! depressed cubic: t³ + pt + qc = 0, λ = t + a/3
            p = b - a*a*onethird
            qc = -(two*a*a*a/27.0_realtype) + a*b*onethird - c
            disc = qc*qc/four + p*p*p/27.0_realtype
            if (disc .le. zero) then
! three real roots (trigonometric solution)
              sqrtp = sqrt(-(p*onethird))
              phi = zero
              if (sqrtp .gt. 1.0e-30_realtype) then
                if (one .gt. -(qc/(two*sqrtp**3))) then
                  y1 = -(qc/(two*sqrtp**3))
                else
                  y1 = one
                end if
                if (-one .lt. y1) then
                  max1 = y1
                else
                  max1 = -one
                end if
                phi = acos(max1)
                t = two*sqrtp*cos(phi*onethird)
              else
                t = zero
              end if
              lambdamax = t + a*onethird
              t = two*sqrtp*cos((phi+two*pival)*onethird)
              if (lambdamax .lt. t + a*onethird) then
                lambdamax = t + a*onethird
              else
                lambdamax = lambdamax
              end if
              t = two*sqrtp*cos((phi+four*pival)*onethird)
              if (lambdamax .lt. t + a*onethird) then
                lambdamax = t + a*onethird
              else
                lambdamax = lambdamax
              end if
            else
! one real root, two complex conjugates
! real root: λ₁ = t + a/3
! complex pair real part: re(λ₂,₃) = (a - λ₁)/2
              r = sqrt(disc)
              if (-(qc*half) + r .ge. 0.) then
                abs3 = -(qc*half) + r
              else
                abs3 = -(-(qc*half)+r)
              end if
              if (-(qc*half) - r .ge. 0.) then
                abs10 = -(qc*half) - r
              else
                abs10 = -(-(qc*half)-r)
              end if
              t = sign(abs3**onethird, -(qc*half) + r) + sign(abs10**&
&               onethird, -(qc*half) - r)
              lambdamax = t + a*onethird
              if (lambdamax .lt. (a-lambdamax)*half) then
                lambdamax = (a-lambdamax)*half
              else
                lambdamax = lambdamax
              end if
            end if
! fallback to gershgorin if eigenvalue computation produces nan
            if (lambdamax .ne. lambdamax) then
              if (a12 .ge. 0.) then
                abs4 = a12
              else
                abs4 = -a12
              end if
              if (a13 .ge. 0.) then
                abs11 = a13
              else
                abs11 = -a13
              end if
              g1 = a11 + abs4 + abs11
              if (a21 .ge. 0.) then
                abs5 = a21
              else
                abs5 = -a21
              end if
              if (a23 .ge. 0.) then
                abs12 = a23
              else
                abs12 = -a23
              end if
              g2 = a22 + abs5 + abs12
              if (a31 .ge. 0.) then
                abs6 = a31
              else
                abs6 = -a31
              end if
              if (a32 .ge. 0.) then
                abs13 = a32
              else
                abs13 = -a32
              end if
              g3 = a33 + abs6 + abs13
              if (g1 .lt. g2) then
                if (g2 .lt. g3) then
                  lambdamax = g3
                else
                  lambdamax = g2
                end if
              else if (g1 .lt. g3) then
                lambdamax = g3
              else
                lambdamax = g1
              end if
            end if
          end if
          if (zero .lt. lambdamax) then
            srclambda(i, j, k) = lambdamax
          else
            srclambda(i, j, k) = zero
          end if
        end do
      end do
    end do
  end subroutine computesrclambda

end module sagammaretheta_fast_b

