            if (addgridvelocities) qs = half*(sfacei(i, j, k)+sfacei(i-1&
&               , j, k))*voli
            uu = xa*w(i, j, k, ivx) + ya*w(i, j, k, ivy) + za*w(i, j, k&
&             , ivz) - qs
            um = zero
            up = zero
            if (uu .lt. zero) um = uu
            if (uu .gt. zero) up = uu
            bb(1, i) = bb(1, i) - up
            dd(1, i) = dd(1, i) + um
            bb(2, i) = bb(2, i) - up
            dd(2, i) = dd(2, i) + um
            bb(3, i) = bb(3, i) - up
